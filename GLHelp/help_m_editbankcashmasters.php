<p><b>&nbsp;&nbsp;&nbsp; Bank/Cash Masters </b>
<ol><b>To Edit Existing Bank Master</b></ol>

<ol><li>Enter the "BANKCODE", "BANK NAME", "MICR", "ADDRESS", "EMAIL", "PHONE", "FAX" and "CONTACT PERSON" in the appropriate boxes provided if you want to change the previously entered values .</li><br/>
<b>Eg: </b>Suppose if you want to change the BANKCODE from "AXIS-BNK" to "CAN-BNK" and BANK NAME from "AXIS BANK" to "Canara Bank" then <br/> you have to enter  <em style="color:#0066FF">CAN-BNK</em> for "BANKCODE" and  <em style="color:#0066FF">Canara Bank</em> for "BANK NAME" . 
<li>Once filled click on 'SAVE' button to save changes to the selected Bank Master or 'CANCEL' to exit with out saving changes to the selected Bank Master.</li>
<br/></ol>
<ol><b>To Edit Existing Cash Master</b></ol>

<ol><li>Enter the "Cash Code" and ""Cash Book Name" if you want to change the previously entered values .</li><br/>
<b>Eg: </b>Suppose if you want to change the Cash Code from "C1" to "CIH", and Cash Book Name from "CASH1" to "CASH IN HAND" then <br/> you have to enter  <em style="color:#0066FF">CIH</em> for "Cash Code"  and  <em style="color:#0066FF">CASH IN HAND</em> for "Cash Book Name". 
<li>Once filled click on 'SAVE' button to save changes to the selected Cash Master or 'CANCEL' to exit with out saving changes to the selected Cash Master.</li>
