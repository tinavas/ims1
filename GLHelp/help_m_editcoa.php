<p><b>&nbsp;&nbsp;&nbsp; Chart of Accounts </b>
<ol><b>To Edit existing Chart of Accounts</b></ol>
<ol>
<li>To Edit existing Chart of Accounts you have to click "VIEW"</li>
<li>Now click on "EDIT" icon provided in the Actions Column of Chart of Accounts</li>
<li>Enter the "DESCRIPTION" in the appropriate box provided </li><br/>
<b>Eg:</b> Suppose if you want to change the Description of Chart of Accounts with code "00104" from "Head Office Cash" to "Sub Office Cash" you have to enter <em style="color:#0066FF">sub Office Cash </em> in "Decription" box Provided.

<li>Once filled click on 'UPDATE' button to save the Changes to the Chart of Accounts or 'CANCEL' to exit with out saving the updates.</li>
