<p><b>&nbsp;&nbsp;&nbsp; Bank/Cash Masters </b>
<ol><b>To Add new Bank/Cash Masters</b></ol>
<ol>
<li>To add a new <em style=" text-decoration:underline">Cash Masters</em> you have to select "Cash" by clicking the radio button provided</li>
<ol>Enter the "CASH CODE" and "CASH BOOK NAME" in the appropriate boxes provided </ol>
<p><b>Eg: </b>Suppose if CASH CODE is "General", CASH BOOK NAME is "Sale Expenses" you have to enter <em style="color:#0066FF">General</em> for "CASH CODE" and <em style="color:#0066FF">Sale Expenses</em> for "CASH BOOK NAME". </p>
<ol>Once filled click on 'SAVE' button to save or 'CANCEL' to exit with out saving.</ol>
<li>To add a new <em style=" text-decoration:underline">Bank Masters</em> you have to select "Bank" by clicking the radio button provided</li>
<ol>Enter the "BANKCODE", "BANK NAME", "MICR", "ADDRESS", "EMAIL", "PHONE", "FAX" and "CONTACT PERSON" in the appropriate boxes provided </ol>
<p><b>Eg: </b>Suppose if BANKCODE is "CAN-BNK", BANK NAME is "Canara Bank" ,MICR# is "000000002" and ADDRESS is "Rajaji Nagar, Bangalore" you have to enter  <em style="color:#0066FF">CAN-BNK</em> for "BANKCODE" , <em style="color:#0066FF">Canara Bank</em> for "BANK NAME" ,<em style="color:#0066FF">000000002</em> for "MICR"  and <em style="color:#0066FF">Rajaji Nagar, Bangalore</em> for "ADDRESS". </p> 
<li>Once filled click on 'SAVE' button to save the Bank/Cash Master or 'CANCEL' to exit with out saving the Bank/Cash Master.</li>
</ol>
