<p><b>&nbsp;&nbsp;&nbsp; Define Schedule </b>
  
<ol><b>To Add new Schedule</b></ol>
<ol>
<li>To add a new Schedule you have to click "ADD" button.</li>
<li>Enter the values of "CODE" and  "DESCRIPTION" in the appropriate boxes provided. </li><br/>
<b>Eg: </b>If Code of Schedule is "c1" and Description is "code1" you have to enter <em style="color:#0066FF">c1</em> for "Code" and <em style="color:#0066FF">code1</em> for "Description". 
<li>you can select the "TYPE" and "PARENT SCHEDULE" from the appropriate dropdowns provided. </li>
<br/>
<b>Eg: </b>If Type of Schedule is "Capital" and Parent Schedule is "Share Capital" you have to select <em style="color:#0066FF">Capital</em> for "Type" and <em style="color:#0066FF">Share Capital</em> for "Parent Schedule". 
<li>Once filled you have to click on 'SAVE' button to save the schedule or 'CANCEL' to exit.</li>
</ol>
