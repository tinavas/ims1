<p><b>&nbsp;&nbsp;&nbsp; Cheque Series   </b>
<ol><b>To Add new Cheque Series </b></ol>
<ol>
<li>To add a new Cheque Series you have to click "ADD".</li>
<li>You have to select "Bank A/c #" from the dropdown provided.</li>
<li>you have to enter "No. of Digits", "No. Of Cheque Leaves" and "Starting No." in the appropriate boxes provided.</li>
<p><b>Eg: </b>Suppose if the Bank A/c # is "432765432102" , No. of Digits is "2", No. Of Cheque Leaves is "75"  and  Starting No. is "77777" you have to select <em style="color:#0066FF">432765432102</em> for "Bank A/c #" ,enter <em style="color:#0066FF">2</em> for "No. Of Digits", "<em style="color:#0066FF">75</em> for "No. Of Cheque Leaves" and <em style="color:#0066FF">77777</em> for "Starting No.".  <br/>
The Ending No. is calculated automatically and displayed <em style="color:#0066FF">77851</em> in "Ending No.". </p>
<li>Once filled click on 'SAVE' button to save and 'CANCEL' to exit.</li>
</ol>
