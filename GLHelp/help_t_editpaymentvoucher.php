<p><b>&nbsp;&nbsp;&nbsp; Payment Voucher   </b>
<ol><b>To Edit existing Payment Voucher </b></ol>
<ol>
<li>Enter the "PAYMENT VOUCHER DATE" from the box provided </li>
<li>To edit existing Payment voucher to <em style="text-decoration:underline">Cash Payment Voucher</em> you have to select "Cash" by clicking the radio button provided</li>
<ol>You have to enter Cr or Dr based on the select value of "Dr/Cr" , "Payees' Name", "Payment Mode" and "Narration"</ol>
<ol>Repeat the above 2 steps if you want</ol>
<p><b>Eg: </b>Suppose if you want change Cr/Dr value of first Payment Cr. from 1000 to 2000 you have to enter value <em style="color:#0066FF">2000</em>.You can repeat this process until all the changes in Cr/Dr values are filled.<br/>
Enter the PAYEES' NAME and NARRATION of the payment.</p>
<ol>Once filled click on 'UPDATE' button to save and 'CANCEL' to exit without saving the changes.</ol>
<li>To edit existing Payment voucher to <em style="text-decoration:underline">Bank Payment Voucher</em> you have to select "Bank" by clicking the radio button provided</li>
<ol>You have to enter Cr or Dr based on the select value of "Dr/Cr" , "Payees' Name", "Payment Mode" and "Narration"</ol>
<ol>Repeat the above 2 steps if you want</ol>
<p><b>Eg: </b>Suppose if you want change Cr/Dr value of first Payment Cr. from 1000 to 2000 you have to enter value <em style="color:#0066FF">2000</em>.You can repeat this process until all the changes in Cr/Dr values are filled.<br/>
Enter the PAYEES' NAME and NARRATION of the payment.</p>
<ol>Once filled click on 'UPDATE' button to save and 'CANCEL' to exit without saving the changes.</ol>
</ol>
NOTE : You cannot edit the Payment Voucher if it is already Authorized.
