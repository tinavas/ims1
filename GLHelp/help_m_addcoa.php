<p><b>&nbsp;&nbsp;&nbsp; Chart of Accounts </b>
<ol><b>To Add new Chart of Accounts</b></ol>
<ol>
<li>To add a new Chart of Accounts you have to click "ADD" button</li>
<li>Enter the "CODE" and "DESCRIPTION" in the appropriate boxes provided </li><br/>
<b>Eg: </b>If Code of Chart of Accounts is "coa1" and Description is "chart of accounts 1" you have to enter <em style="color:#0066FF">coa1</em> for "Code" and <em style="color:#0066FF">chart of accounts 1</em> for "Description". 
<li>you can select the "TYPE","CONTROLL TYPE" and "SCHEDULE" from the dropdown lists. </li>
<br/>(Note: You can add a new Schedule directly by clicking on the "+" symbol provided here ) 
<p><b>Eg: </b>If Type of Chart of Accounts is "Liability", CONTROLL TYPE is "Bank OD A/c" and Schedule is "Short Term Borrowings" you have to select <em style="color:#0066FF">Liability</em> for "Type" , <em style="color:#0066FF">Bank OD A/c</em> for "CONTROLL TYPE" and <em style="color:#0066FF">Short Term Borrowings</em> for "Schedule". </p> 

<li>Once filled click on 'SAVE' button to save the Chart of Accounts or 'CANCEL' to exit without saving.</li>
