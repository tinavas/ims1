<p><b>&nbsp;&nbsp;&nbsp; Receipt Voucher   </b>
<ol><b>To Add new Receipt Voucher </b></ol>
<ol>
<li>Enter the "RECEIPT VOUCHER DATE" from the box provided </li>

<li>To add a new <em style="text-decoration:underline">Cash Receipt Voucher</em> you have to select "Cash" by clicking the radio button provided</li>
<ol>You have to select the "Cash Code" from the dropdown provided.</ol>
<ol>You have to select the "CODE" from the dropdown provided </ol>
<ol>You have to enter Cr or Dr based on the select value of "Dr/Cr" , "Payers' Name", "Payment Mode" and "Narration"</ol>
<ol>Repeat the above 2 steps if you want</ol>
<p><b>Eg: </b>Suppose if CASH CODE No. "General" is selected, CODE "00104", DESCRIPTION "Head Office Cash" and Cr/Dr "Cr" values are automatically displayed. You can Select the date of the Receipt such as <em style="color:#0066FF">03.01.2011</em> <br/>
Now you have to enter Cr value suppose as <em style="color:#0066FF">1000</em>.You can repeat this process until all the receipts are filled.<br/>
Enter the PAYERS' NAME and NARRATION of the payment.</p>
<ol>Once filled click on 'SAVE' button to save and 'CANCEL' to exit.</ol>
<li>To add a new <em style="text-decoration:underline">Bank Receipt Voucher</em> you have to select "Bank" by clicking the radio button provided</li>
<ol>You have to select the "BANK A/C No" from the dropdown provided</ol>
<ol>You have to select the "CODE" from the dropdown provided </ol>
<ol>You have to enter Cr or Dr based on the select value of "Dr/Cr" , "Payers' Name", "Payment Mode" and "Narration"</ol>
<ol>Repeat the above 2 steps if you want</ol>
<p><b>Eg: </b>Suppose if BANK A/C No "432765432102" is selected, CODE  "00501", DESCRIPTION "ADH" and Cr/Dr "Cr" values are automatically displayed. You can Select the date of the Receipt such as <em style="color:#0066FF">03.01.2011</em> <br/>
Now you have to enter Cr value suppose as <em style="color:#0066FF">1000</em>.You can repeat this process until all the receipts are filled.<br/>
Select PAYMENT MODE such as <em style="color:#0066FF">Cheque</em> or <em style="color:#0066FF">others</em> should be selected.<br/>
(Note: if Payment Mode <em style="color:#0066FF">Cheque</em> is selected CHEQUE NO. should be entered suppose as <em style="color:#0066FF">053456</em>.<br/>

Enter the PAYERS' NAME and NARRATION of the payment.</p>
<ol>Once filled click on 'SAVE' button to save and 'CANCEL' to exit.</ol>
