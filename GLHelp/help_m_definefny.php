<p><b>&nbsp;&nbsp;&nbsp;Define Financial Year </b></p>
<ol>
To define new Financial Year
<li>You have to select the date from which Accounts starts and date when the Accounts close from the calendar provided.</li>
<br/>
 <b>Eg:</b> If starting date of Accounts is "Jan 01, 2010" and closing date is "Dec 31, 2010" you have to select <em style="color:#0066FF">01.01.2010</em>  in "From" and <em style="color:#0066FF">31.12.2010</em> in "To" .
<li>Once filled click on 'SAVE' button to save or 'CANCEL' to exit.</li>
</ol>
