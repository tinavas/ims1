<p><b>&nbsp;&nbsp;&nbsp; Bank/Cash to CoA Mapping  </b>
<ol><b>To Add new Bank/Cash to CoA Mapping </b></ol>
<ol>
<li>To add a new Bank/Cash to CoA Mapping  you have to click "ADD" button</li>
<li>To add a new <em style=" text-decoration:underline">Cash code with CoA Mapping</em> you have to select "Cash" by clicking the radio button provided</li>
<ol>Now you have to select the "CASH CODE" and "COA CODE" from the dropdowns provided </ol>
<p><b>Eg: </b>Suppose if CASH CODE is "General", COA CODE is "00104" you have to select <em style="color:#0066FF">General</em> for "CASH CODE" and <em style="color:#0066FF">00104</em> for "COA CODE". </p>
<ol>Once filled click on 'SAVE' button to save or 'CANCEL' to exit without saving.</ol>
<li>To add a new <em style=" text-decoration:underline">Bank code with CoA Mapping</em> you have to select "BANK" by clicking the radio button provided</li>
<ol>Now you have to select the "BANK CODE" and  "COA CODE"  from the dropdowns provided </ol>
<ol>Enter the "BANK A/C NUMBER" in the box provided </ol>
<p><b>Eg: </b>Suppose if BANK CODE is "ADH-OD", COA CODE is "00502" and BANK A/C NUMBER is "432765432102" you have to select <em style="color:#0066FF">ADH-OD</em> for "BANK CODE" , <em style="color:#0066FF">00502</em> for "COA CODE" and enter <em style="color:#0066FF">432765432102</em> for "BANK A/C NUMBER". </p>
<ol>Once filled click on 'SAVE' button to save or 'CANCEL' to exit with out saving.</ol>


