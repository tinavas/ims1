<p><b>&nbsp;Flock Wise Medication</b></p>
<p>For Flock Wise Medication</p>
<ol>
<li>You have to select Flock.<br/>
<b>Eg :</b>Suppose the Flock Code is "L1-S57" you have to select <em style="color:#0066FF">L1-S57</em> for "Flock".</li>
<li>You have to select date.<br>
<b>Eg :</b>Suppose date is "Sep 28 2011" you have to select <em style="color:#0066FF">28.09.2011</em> for "Date".</li>

<li>You have to select Disease.<br/>
<b>Eg :</b>Suppose the Disease is "Arizonosis" you have to select <em style="color:#0066FF">Arizonosis</em> for "Disease". As and when you select disease the Tentative cause,Symptoms and Prevention of that disease will be displayed automatically.</li>

<li>You have to enter no of Female Birds and Male Birds affected by the disease.<br/>
<b>Eg :</b>Suppose if no of Female Birds are "20" and no of Male Birds are "15" you have to enter <em style="color:#0066FF">20</em> for "Female" and <em style="color:#0066FF">15</em> for "Male".</li>
<li>If you want to modify the displayed Tentative Cause, Symptoms and Prevention you can modify those. </li>

<li>Once filled click "SAVE" button to Save Flock Wise Medication or click "CANCEL" button to exit without Saving Flock Wise Medication.</li>
