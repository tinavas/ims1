<p><b>&nbsp;&nbsp;&nbsp;Add New Egg Transfer</b></p>
<ol><b>To Add New Egg Transfer</b>
<br/>
<br/>
<li>You have to select "Date" and "Warehouse".</li>
<br/>
<br/>
<li>Now select values for "Egg Code" or "Egg Description"<br/>
As and when you select the "Egg Code" the value of "Egg Description" will be displayed automatically <br/>
If you select the "Egg Description" the value of "Egg Code" will be displayed automatically  </li><br/>
<br/>
<li>
Now Enter the value in "no of Eggs" (Number of Eggs to be transferred from warehouse to other warehouse).<br/>
</li><br/>
<li>Now select "To" (Place to which Eggs are to be transferred) and "code" <br/>
As and when you select the "Code" the value of "Description" will be displayed automatically<br/>
If you select the "Description" the value of "Code" will be displayed automatically<br/>
</li><br/>
<li>
Suppose if you enter the "code" same as "eggcode" then you will get a message as "Cannot transfer to the same type"</br>
If you get that message click on "ok" and enter the code again which is not same as "Eggcode"<br/>
</li><br/>
<li>
Now enter value in "Total Eggs" ( i.e., the number of eggs to be transferred from the "no of eggs")<br/>
</li>
<br/>
<b>Eg : </b>Suppose the Egg Transfer Date is "6th June, 2011",if you want to transfer Eggs from warehouse "Godown1" to "Godown1" from Eggcode "EG101", Egg Description "PULLET", No of Eggs "122", Code " EG102 " , Description " MEDIUM ", and Total Eggs " 122" <br/>
you have to select <em style="color:#0066FF">06.06.2011</em> for "Date", <em style="color:#0066FF">Godown1</em> for "Warehouse"<em style="color:#0066FF"> EG101 or PULLET </em> for "EggCode" or "Egg Description", enter <em style="color:#0066FF">122</em> for "No of Eggs",<em style="color:#0066FF">Godown1 </em> for "To", <em style="color:#0066FF">EG102 or MEDIUM </em> for "Code" or "Description", <em style="color:#0066FF">122</em> for "TotalEggs"<br/>
<br/>
<li>Repeat the above 4,5,6 steps until all the Egg transfer details are filled.</li><br/>
<br/>
<li><b> NOTE :</b>Now check whether the value in the "No of Eggs" matches with the "Total"</br>
If both the values matches the save button is enabled other wise the save button is disabled.
If it is disabled change the values of Total Eggs to be transferred and check whether the "Total" matches with "No of eggs" or not</li>
<br/>
<br/>
 <li>Once filled click on 'SAVE' button to save the Egg Transfer details or 'CANCEL' to exit without saving the Egg Transfer details.</li><strong></strong>
</ol>
