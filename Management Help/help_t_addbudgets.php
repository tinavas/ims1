<p><b>&nbsp;&nbsp;&nbsp;Add New Budgets</b></p>
<ol><b>To Add New Budgets</b>
<li>You have to select "Cost Centre", "Month", "Year", "Coacode", "Description", "Cr/Dr" and enter "Amount".</li>

<br/>
<b>Eg : </b>Suppose the Cost Centre is "Chicken Shop", Month "Apr", Year is "2011", Coacode is "99999", Description is "Suspense Account", Cr/Dr is "Cr", and Amount is "1500" <br/>
you have to select <em style="color:#0066FF">Chicken Shop</em> for "Cost Centre", <em style="color:#0066FF">Apr</em> for "Month", <em style="color:#0066FF">2011</em> for "Year", <em style="color:#0066FF">99999</em> for "Coacode", <em style="color:#0066FF">Suspense Account</em> for "Description", <em style="color:#0066FF">Cr</em> for "Cr/Dr" and <em style="color:#0066FF">1500 </em> for "Amount".<br/>
<img src="bud.jpg" >
<li>Repeat the above step until all the Budget details are filled.</li>
 <li>Once filled click on 'SAVE' button to save the Budget details or 'CANCEL' to exit without saving the Budget details.</li><strong></strong>
