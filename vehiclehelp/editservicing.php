<p><b>&nbsp;To Edit Vehicle Servicing Details</b></p>

<ol>
<li>You have to select servicing date.<br>
<b>Eg :</b>Suppose date is "05.01.2012" then, you have to select <em style="color:#0066FF">05.01.2012</em> for "Date".</li>

<li>You have to select vehicle type whether it is car or lorry or bus etc...<br>
<b>Eg :</b>Suppose if  it is "Car" then, you have to select <em style="color:#0066FF">Car</em> as "Vehicle Type".</li>

<li>You have to select Vehicle Number.(the unique no assigned to that vehicle).<br>
<b>Eg :</b>Suppose if it is "46061" then you have to select<em style="color:#0066FF">46061</em> as "Vehicle Number".</li>

<li>you have to select  Service Code.<br>
<b>Eg :</b>Suppose if  it is "S3" then, you have to select <em style="color:#0066FF">S3</em> as "Service Code".</li>

<li>You have to select next servicing date.<br>
<b>Eg :</b>Suppose date is "05.04.2012" then, you have to select <em style="color:#0066FF">05.04.2012</em> for "T.Next S.Date".</li>

<li> You have to enter Warranty in days.<br>
<b>Eg :</b>Suppose if Warranty is "90 days" then, you have to enter<em style="color:#0066FF">90</em> as "Warranty Date".</li>

<li> You have to enter service charges.<br>
<b>Eg :</b>Suppose if service charges are  "4500 Rupees" then, you have to enter<em style="color:#0066FF">4500</em> as "service charges".</li>
<li>you have to select driver of vehicle.<br>
<b>Eg :</b>Suppose if  driver is "Abdul" then, you have to select <em style="color:#0066FF">Abdul</em> as "Driver".</li>


<li> If you want to write something about Renewal write it down as Remarks.</li>

<li>Once filled click "SAVE" button to Save "Vehicle Servicing" or click "CANCEL" button to exit without Saving "Vehicle Servicing".</li>
