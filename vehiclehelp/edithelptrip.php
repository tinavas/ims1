<p><b>&nbsp;To Edit Vehicle Trip Details</b></p>

<ol>

<li>You have to select vehicle type whether it is car or lorry or bus etc...<br>
<b>Eg :</b>Suppose if  it is "Car" then, you have to select <em style="color:#0066FF">Car</em> as "Vehicle Type".</li>

<li>You have to select Vehicle Number.(the unique no assigned to that vehicle).<br>
<b>Eg :</b>Suppose if it is "46061" then you have to select<em style="color:#0066FF">46061</em> as "Vehicle Number".</li>

<li>you have to select driver of vehicle.<br>
<b>Eg :</b>Suppose if  driver is "Azim" then, you have to select <em style="color:#0066FF">Azim</em> as "Driver".</li>

<li>You have to select start date.<br>
<b>Eg :</b>Suppose start date is "05.01.2012" then, you have to select <em style="color:#0066FF">05.01.2012</em> for " Start Date".</li>

<li>You have to enter start time.<br>
<b>Eg :</b>Suppose start time is "07.30" then, you have to enter<em style="color:#0066FF">07.30</em> for " Start Time".</li>

<li>You have to select start Place.<br>
<b>Eg :</b>Suppose start place is "Bengalore" then, you have to select <em style="color:#0066FF">Bengalore</em> for " Start Place".</li>
<li> You have to enter start reading.<br>
<b>Eg :</b>Suppose if starting reading is  "4587" then, you have to enter<em style="color:#0066FF">4587</em> as "Start Reading".</li>

<li>You have to select end date.<br>
<b>Eg :</b>Suppose end date is "05.01.2012" then, you have to select <em style="color:#0066FF">05.01.2012</em> for " End Date".</li>

<li>You have to enter end time.<br>
<b>Eg :</b>Suppose end time is "07.30" then, you have to enter<em style="color:#0066FF">07.30</em> for " End Time".</li>

<li>You have to select End Place.<br>
<b>Eg :</b>Suppose  End place is "Mysore" then, you have to select <em style="color:#0066FF">Mysore</em> for " End Place".</li>
<li> You have to enter End reading.<br>
<b>Eg :</b>Suppose if end reading is  "4882" then, you have to enter<em style="color:#0066FF">4882</em> as "End Reading".</li>

<li> You have to enter Expences Incurred.<br>
<b>Eg :</b>Suppose if expences are  "3000 Rupees" then, you have to enter<em style="color:#0066FF">3000</em> as "Expences Incurred".</li>
<li>You have to select whether the vehicle is travelled with load or without load.<br>
<b>Eg :</b>Suppose  it is "With load" then, you have to select <em style="color:#0066FF">With load</em>.</li>

<li> If you want to write something about Renewal write it down as Remarks.</li>

<li>Once filled click "SAVE" button to Save "Vehicle Trip" or click "CANCEL" button to exit without Saving "Vehicle Trip".</li>
