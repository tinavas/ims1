<p><b>&nbsp;Fuel Filling</b></p>

<ol><li>You have to select date on which fuel is filling.<br>
<b>Eg :</b>Suppose date is "05.01.2012" then, you have to select <em style="color:#0066FF">05.01.2012</em> for "Date".</li>
<li>You have to select vehicle type whether it is car or lorry or bus etc...<br>
<b>Eg :</b>Suppose if  it is "Car" then, you have to select <em style="color:#0066FF">Car</em> as "Vehicle Type".</li>
<li>You have to select Vehicle Number.(the unique no assigned to that vehicle).<br>
<b>Eg :</b>Suppose if it is "46061" then you have to select<em style="color:#0066FF">46061</em> as "Vehicle Number".</li>
<li>you have to select fuel type. whether it is petrol or diesel etc...<br>
<b>Eg :</b>Suppose if  it is "Petrol" then, you have to select <em style="color:#0066FF">petrol</em> as "Fuel Type".</li>

<li> You have to enter how much fuel is filled in liters.<br>
<b>Eg :</b>Suppose if fuel filled is "15 liters" then, you have to enter<em style="color:#0066FF">15</em> as "Fuel Filled".</li>

<li> You have to enter cost of fuel per liters.<br>
<b>Eg :</b>Suppose if fuel cost is "75 Rupees" then, you have to enter<em style="color:#0066FF">75</em> as "Fuel Cost".</li>
<li> You have to enter the current reading of vehicle.<br>
<b>Eg :</b>Suppose if current reading is "6756" then, you have to enter<em style="color:#0066FF">6756</em> as "Current Reading".
<li>you have to select driver of vehicle.<br>
<b>Eg :</b>Suppose if  driver is "Abdul" then, you have to select <em style="color:#0066FF">Abdul</em> as "Driver".</li>

<li> If you want to write something about fuel filling write it down as Remarks.</li>

<li>Once filled click "SAVE" button to Save "Fuel Filled entry" or click "CANCEL" button to exit without Saving "Fuel Filled entry".</li>
