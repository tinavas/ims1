<p><b>&nbsp;&nbsp;&nbsp;Add New Stock Transfer(Rental)</b></p>
<ol><b>To Add New Stock Transfer(Rental)</b>
<li>You have to select "Date" and "Warehouse".</li>
<li>Now select values for ""Category", "Code" and "To"(Place to which stock to be transferred).<br/>
As and when you select the "Item Code" the values of "Description" and "Units" will be displayed automatically. </li>
<li>Now enter "Quantity", "Price", "DC#"(Delivery Challan), T.Cost(Transportation Cost), "Vehicle No." and "Remarks". </li>
<br/>
<b>Eg : </b>Suppose the Stock Transfer Date is "30th August, 2011",if you want to transfer an item from warehouse "Kota1" to "Kota2" for flock "Kota-2-@-1" from Category "Broiler Chicks", Item Code "BROC101", Quantity "1000", Price/Unit "20", DC# "8914", Transportation Cost "50",  and Vehicle No "KA1 C 7458",<br/>
you have to select <em style="color:#0066FF">30.08.2011</em> for "Date", <em style="color:#0066FF">Kota-1</em> for "Warehouse"<em style="color:#0066FF"> Broiler Chicks</em> for "Category", <em style="color:#0066FF">BROC101</em> for "Code", <em style="color:#0066FF">Kota-2</em> for "To" and <em style="color:#0066FF">Kota-2-@-1</em> for "Flock" , enter <em style="color:#0066FF">1000</em> for "Quantity", <em style="color:#0066FF">20</em> for "Price/Unit", <em style="color:#0066FF">8914</em> for "DC#", <em style="color:#0066FF">50</em> for "T.Cost" and <em style="color:#0066FF">KA1 C 7458</em> for "Vehicle No."<br/><br>
<img src="Images/addstocktransferrental.jpg" width="897"/>
<br/>
<li>Repeat the above 2 steps until all the stock transfer details are filled.</li>
 <li>Once filled click on 'SAVE' button to save the Stock Transfer details or 'CANCEL' to exit without saving the Stock Transfer details.</li><strong></strong>
</ol>
