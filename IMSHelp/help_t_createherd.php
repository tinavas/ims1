<p><b>&nbsp;Create Herd</b></p>
<p>To Create New Herd</p>

<ol><li>You have to select Unit Code or Unit Description.(if you select Unit Code then Unit Description will be displayed automativally, or if you select Unit Description then Unit Code will be displayed automatically.)<br>
<b>Eg :</b>Suppose if Unnit Code is "PG-A10", then you have to select <em style="color:#0066FF">PG-A10</em> for "Unit Code". or if Unit Description  is "Unit No 10" then you have to select <em style="color:#0066FF">Unit No 10</em> for "Unit Description".</li>
<li>You have to select House Code or House Description.(if you select House Code then House Description will be displayed automativally, or if you select House Description then House Code will be displayed automatically.)<br>
<b>Eg :</b>Suppose if House Code is "PG-A-H10", then you have to select <em style="color:#0066FF">PG-A-H10</em> for "Code". or if description  is "House No 10" then you have to select <em style="color:#0066FF">house No 10</em> for "House Description".</li>

<li>You have to enter "Herd Code" and "Herd Description".<br>
<b>Eg :</b>Suppose if Herd Code is "PG-A-HD15" and Herd Description is "Herd No 15", then you have to enter <em style="color:#0066FF">PG-A-HD15</em> for "Herd Code" and <em style="color:#0066FF">Herd No 15</em> for "Herd Description".</li>

<li>You have to select the Start date of the Herd.<br>
<b>Eg :</b>Suppose if Stard date is "01.10.2011", then you have to select <em style="color:#0066FF">01.10.2011</em> for Stard date.  </li>

<li>You have to enter Age.<br>
<b>Eg :</b>Suppose if Age is "18" days, then you have to enter<em style="color:#0066FF">18</em> for Age.</li>

<li>You have to enter No of Show Openings and Boar Openings.<br>
<b>Eg :</b>Suppose if Show Openings are  "400" and Boar Openings are 300, then you have to enter<em style="color:#0066FF">400</em> for Show Opening and <em style="color:#0066FF">300</em> for Boar Opening .</li>


<li>Once filled click "SAVE" button to save Herd or click "CANCEL" button to exit without Saving Herd.</li>
</ol>
