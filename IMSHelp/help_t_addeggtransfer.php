<p><b>&nbsp;&nbsp;&nbsp;Add New Egg Transfer</b></p>
<ol><b>To Add New Stock Transfer</b>
<li>You have to select "Date" and "Warehouse".</li>
<li>Now select values for "EggCode" or "EggDescription".<br/>
if you select the "EggCode" then the values of "EggDescription" will be diplayed automatically OR 
if you select the "EggDescription" then the values of "EggCode" will be diplayed automatically<br>
 </li>
<br/>
<b>Eg : </b>Suppose the Egg Transfer Date is "28th Oct, 2011",if you want to transfer an item from "Puttur",EggCode is "EG101" ,EggDescription is "Table Egg" ,Total Eggs is "1000" and Wastage is "50" To "Heddurga" ,Code is "EG101",Description is "Table Eggs",Quantity of Eggs to this particular ware house is "800" and Price is "2.3 Rs" <br/>
you have to select <em style="color:#0066FF">28.10.2011</em> for "Date", <em style="color:#0066FF">Puttur</em> for "Ware house", <em style="color:#0066FF">Eg101</em> for "EggCode", <em style="color:#0066FF">Table Eggs</em> for "EggDescription",<em style="color:#0066FF">1000</em> for "Eggs",<em style="color:#0066FF">50</em> for "Wastage"<br><br><em style="color:#0066FF">Heddurga</em> for "To",<em style="color:#0066FF">Eg101</em> for "Code",<em style="color:#0066FF">Table Eggs</em> for "Description",<em style="color:#0066FF">800</em> for "Total Eggs",<em style="color:#0066FF">2.3</em> for "Price",<br/>
<img src="Images/addeggtransfer.jpg" alt="Egg Tansfer" >
<li>Repeat the above step until all the Egg transfer details are filled.</li>
 <li>Once filled click on 'SAVE' button to save the Stock Transfer details or 'CANCEL' to exit without saving the Stock Transfer details.</li><strong></strong>
