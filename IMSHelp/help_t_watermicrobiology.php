you have to select <em style="color:#0066FF">06.06.2011</em> for "sampleDate",<em style="color:#0066FF">31.08.2011</em> for "reportDate",  <em style="color:#0066FF">Breeder</em> for "FarmType"<em style="color:#0066FF"> B1 </em> for "FarmName", select <em style="color:#0066FF">Borewell Water</em> for "Sample",enter <em style="color:#0066FF">23</em> for "Ecoil",<em style="color:#0066FF">31</em> for "Ph",<em style="color:#0066FF">1</em> for "hardness".<br/>  
<br/>
<li>
