<p><b>&nbsp;&nbsp;&nbsp;Edit Existing Stock Transfer</b></p>
<ol><b>To Edit Existing Stock Transfer</b>
<li>You have to select "Date" and "Warehouse" if you want any changes in previously selected values.</li>
<li>Now select values for ""Category", "Code" and "To"(Place to which stock to be transfered) if you want any changes in previously selected values. </li>
<li>Now enter "Quantity", "Price", "DC#"(Delivery Challan), T.Cost(Transportation Cost), "Vehicle No.", "Driver" and "Remarks" if you want any changes in previous values. </li>
<br/>
<b>Eg : </b>Suppose if you want to change the Stock Transfer Date is from "1st Hune, 2011" to "6th June, 2011" then you have to select <em style="color:#0066FF">06.06.2011</em> for "Date".
<li>Repeat the above 2 steps until all the stock transfer details are filled.</li>
 <li>Once filled click on 'SAVE' button to save changes to the selected Stock Transfer details or 'CANCEL' to exit without saving  changes to the selected Stock Transfer details.</li>
</ol>
