<p><b>&nbsp;&nbsp;&nbsp;Add New Stock Transfer</b></p>
<ol><b>To Add New Stock Transfer</b>
<li>You have to select "Date" and "Warehouse".</li>
<li>Now select values for ""Category", "Code" and "To"(Place to which stock to be transferred).<br/>
As and when you select the "Item Code" the values of "Description" and "Units" will be displayed automatically. </li>
<li>Now enter "Quantity", "Price", "DC#"(Delivery Challan), T.Cost(Transportation Cost), "Vehicle No.", "Driver" and "Remarks". </li>
<br/>
<b>Eg : </b>Suppose the Stock Transfer Date is "6th June, 2011",if you want to transfer an item from Godown "Godown1" to "Healthy PF" from Category "Broiler Feed", Item Code "FD107", Quantity "122", Price/Unit "12", DC# "3400", Transportation Cost "200",  and Vehicle No "KA10 C 5783",<br/>
you have to select <em style="color:#0066FF">06.06.2011</em> for "Date", <em style="color:#0066FF">Godown1</em> for "Warehouse"<em style="color:#0066FF"> Broiler Feed</em> for "Category", <em style="color:#0066FF">FD107</em> for "Code", <em style="color:#0066FF">Healthy PF</em> for "To", enter <em style="color:#0066FF">122</em> for "Quantity", <em style="color:#0066FF">12</em> for "Price/Unit", <em style="color:#0066FF">3400</em> for "DC#", <em style="color:#0066FF">200</em> for "T.Cost" and <em style="color:#0066FF">KA10 C 5783</em> for "Vehicle No."<br/>

<li>Repeat the above 2 steps until all the stock transfer details are filled.</li>
 <li>Once filled click on 'SAVE' button to save the Stock Transfer details or 'CANCEL' to exit without saving the Stock Transfer details.</li><strong></strong>
</ol>
