<p><b>&nbsp;&nbsp;&nbsp;Stock Adjustment </b></p>
<ol><b>For Stock Adjustment</b>
<li>You have to select "Date".<br>
<b>Eg : </b>Suppose if date is October 11 2011, then you have to select <em style="color:#0066FF">11.10.2011</em> for date.</li>

<li>You have to select "Unit".<br>
<b>Eg : </b>Suppose if Unit is "Kamal | Godown", then you have to select <em style="color:#0066FF">Kamal | Godown</em> for Unit.</li>

<li>You have to select "Coa Code".<br>
<b>Eg : </b>Suppose if Coa Code is "IE105", then you have to select <em style="color:#0066FF">IE105</em> for Coa Code.</li>

<li>You have to select "Category" of item.<br>
<b>Eg : </b>Suppose if Category is "Egss", then you have to select <em style="color:#0066FF">Egss</em> for Category.</li>

<li>You have to select "Item Code".<br>
<b>Eg : </b>Suppose if Item Code is "EG102", then you have to select <em style="color:#0066FF">EG102</em> for Item Code.(or) If you don't know item code then you can select Item Description.</li>

<li>You have to select "Add/Deduct" of item.<br>
<b>Eg : </b>Suppose if you are adding then you have to select <em style="color:#0066FF">Add</em>  or ,if you are removing then you have to select <em style="color:#0066FF">Deduct</em> for Add/Deduct.</li>
<li>You have to enter quantity.<br>
<b>Eg : </b>Suppose if quantity is 200 then you have to select <em style="color:#0066FF">200</em> for Quantity.</li>
<li> once filled click save button to save Stock Adjustment or click cancel to exit with out saving Stock Adjustment.</li>
</ol><br>
