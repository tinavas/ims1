<p><b>&nbsp;Create Unit</b></p>
<p>To Create New Unit</p>

<ol><li>You have to select piggery code or piggery description.(if you select code then description will be displayed automativally, or if you select description then code will be displayed automatically.)<br>
<b>Eg :</b>Suppose if code is "PG101", then you have to select <em style="color:#0066FF">PG101</em> for "Code". or if description  is "Piggery No 101" then you have to select <em style="color:#0066FF">Piggery No 101</em> for "Description".</li>
<li>You have to enter "Unit Code" and "Unit Description".<br>
<b>Eg :</b>Suppose if Unit Code is "PG-A10" and Unit Description is "Unit No 10", then you have to enter <em style="color:#0066FF">PG-A10</em> for "Unit Code" and <em style="color:#0066FF">Unit No 10</em> for "Description".</li>

<li>Once filled click "SAVE" button to save Unit or click "CANCEL" button to exit without Saving Unit.</li>
</ol>
