<p><b>&nbsp;&nbsp;&nbsp;Add New Item Master</b></p>
<ol><b>To Add new Item Master</b>
<li>You have to enter the values of "Item Code" and "Description"</li> 
<li>You have to select the values of "Category", "Valuation Method", " Units Of Measure", "Type", "Usage", "Source", "Type", "Item A/C".<br/></li>

<li>
If Selected Value of Usage is "Sale"  you also have to select values for "COGS A/C", "Sales A/C" and "Sales Return".<br/>
<li>
If Selected Value of Usage is "General Consumption"  you also have to select values for "CONSUMPTION A/C".<br/> 
    
<li>
If the selected value of valuation Method is "Standard Costing"  you have to enter value for "Standard Cost".<br/>


<b>Eg : </b>Suppose Item Code is "item001", Description is "ItemMaster001", Category is "Consumables", Units Of Measure is "Numbers", Usage is "General Consumption", Type is "Others", Valuation Method is "Weighted Average", Item A/C "IT401",Consumption A/C "COS401" and Source is "Purchased"  then <br/>
you have to enter <em style="color:#0066FF">item001</em> for "Item Code", <em style="color:#0066FF">ItemMaster001</em> for "Description", select <em style="color:#0066FF">Consumables</em> for "Category", <em style="color:#0066FF">Numbers</em> for "Units Of Measure", <em style="color:#0066FF">General Consumption</em> for "Usage", <em style="color:#0066FF">Others</em> for "Type", <em style="color:#0066FF">Weighted Average</em> for "Valuation Method", <em style="color:#0066FF">IT401</em> for "Item A/C",, <em style="color:#0066FF">COS401</em> for "Consumption A/C", <em style="color:#0066FF">00104</em> for "Price Variance A/C",<em style="color:#0066FF"> Purchased</em> for "Source".</li>
 <li>Once filled click on 'SAVE' button to save the Item Master or 'CANCEL' to exit without saving.</li>
</ol>
