<p><b>&nbsp;Create House</b></p>
<p>To Create New House</p>

<ol><li>You have to select Unit Code or Unit Description.(if you select code then description will be displayed automativally, or if you select description then code will be displayed automatically.)<br>
<b>Eg :</b>Suppose if code is "PG-A10", then you have to select <em style="color:#0066FF">PG-A10</em> for "Code". or if description  is "Unit No 10" then you have to select <em style="color:#0066FF">Unit No 10</em> for "Description".</li>
<li>You have to enter "House Code" and "House Description".<br>
<b>Eg :</b>Suppose if House Code is "PG-A-H10" and House Description is "House No 10", then you have to enter <em style="color:#0066FF">PG-A-H10</em> for "House Code" and <em style="color:#0066FF">House No 10</em> for "Description".</li>
<li>You have to select House Type.<br>
<b>Eg :</b>Suppose if House Type is "Dry Sow", then you have to select <em style="color:#0066FF">Dry Sow</em> for "House Type".</li>
<li>You have to enter House Capavity.<br>
<b>Eg :</b>Suppose if House Capacity is "4000", then you have to enter <em style="color:#0066FF">4000</em> for "House Capacity".</li>
<li>Once filled click "SAVE" button to save House or click "CANCEL" button to exit without Saving House.</li>
</ol>
