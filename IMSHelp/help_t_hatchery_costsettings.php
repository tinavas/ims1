<p><b>&nbsp;&nbsp;&nbsp;Hatchery Cost Settings</b></p>
<ol><b>To save Hatchery Cost Settings</b>
<br/>
<br/>
<li>You have to select "From Date" and "To Date".</li>
<br/>
<br/>
<li>Now Enter the values in the "Hatch Cost" and "Transfer and Package Cost"<br/>
<b>Eg : </b>Suppose the From Date is "6th June, 2011",To Date is "8th June, 2011",  hatch " 122" ,Transfer and Package Cost " 2000"  <br/>
you have to select <em style="color:#0066FF">06.06.2011</em> for "From Date",  <em style="color:#0066FF">08.06.2011</em> for "To Date", enter <em style="color:#0066FF">122</em> for "Hatch cost", enter <em style="color:#0066FF">2000</em> for "Transfer and package cost"<br/>
<br/>
 <li>Once filled click on 'SAVE' button to save the hatchery cost details or 'CANCEL' to exit without saving the cost details.</li><strong></strong>
