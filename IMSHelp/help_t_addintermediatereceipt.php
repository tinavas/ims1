<p><b>&nbsp;&nbsp;&nbsp;Add New Intermediate Receipt</b></p>
<ol><b>To Add new Intermediate Receipt</b>
<li>First You have to select the value of "Date" of Receipt.</li>
<li>You can select "Category" and "Item Code" from dropdowns.<br/>
As and when you select the "Item Code" the values of "Description" and "Units" will be displayed automatically.</li>
<li>Now enter values for "Quantity"(Quantity of Item to be added to the Stock), "Rate/Unit", "Lot#", "Serial No" and select "Warehouse".</li>
<li>Select the "COA"(A/C Code to which the amount i.e Quantity*(Rate/Unit) should be Debited ).</li>
<li>Repeat the steps 2, 3 and 4 until you fill all the details of all Items.</li><br/>
<b>Eg : </b>Suppose the Receipt Date is "6th JAN, 2011",if you want to add 2 items <br/>
first Item with Category "Birds", Item Code "BRD101", Quantity "122", Price/Unit "12" and COA "IE103",<br/>
second Item with Category "Eggs", Item Code "EG101", Quantity "100", Price/Unit "3" and COA "IE112" then<br/>
For first Item you have to select <em style="color:#0066FF">Birds</em> for "Category", <em style="color:#0066FF">BRD101</em> for "Item Code", <em style="color:#0066FF">IE103</em> for "COA", enter <em style="color:#0066FF">122</em> for "Quantity" and <em style="color:#0066FF">12</em> for "Price/Unit<br/>
For Second Item you have to select <em style="color:#0066FF">Eggs</em> for "Category", <em style="color:#0066FF">EG101</em> for "Item Code", <em style="color:#0066FF">IE112</em> for "COA" enter <em style="color:#0066FF">100</em> for "Quantity" and <em style="color:#0066FF">3</em> for "Price/Unit.    
 <li>Once filled click on 'SAVE' button to save the Intermediate Receipt or 'CANCEL' to exit without saving.</li>
</ol>
