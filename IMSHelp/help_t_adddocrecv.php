<ol><b>To Add Document Receive Record</b>
<br/>
<br/>
<li>You have to select "Date" and "Unique Id number"(In second box).</li>
<br/>
<br/>
<li>
If you select "Uid" you will get "Client" automatically.Now Select "Employee". <br/>
</li><br/>
<li>
Select "Period From" and "Period To".
</li>
<br/><br/>
<li>
Select "Form Type" and Click Browse to select a document.</li><br/><br/>

