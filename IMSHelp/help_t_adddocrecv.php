you have to select <em style="color:#0066FF">06.06.2011</em> for "Date",<em style="color:#0066FF">APY78DT657</em> for "Uid " <em style="color:#0066FF">XYZ</em> for "Employee ",<em style="color:#0066FF">06.07.2011</em> for "Period From",<em style="color:#0066FF">16.07.2011</em> for "Period To",select <em style="color:#0066FF">Form1</em> for "Form Type",select <em style="color:#0066FF">xyz.txt</em> for "Attach Document"<br/>  
<br/>

<li>Repeat 4th step until all the details are filled.</li><br/>
