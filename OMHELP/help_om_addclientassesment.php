<p><b>&nbsp;Client Assesment</b></p>
<p>Add Client Assesment</p>
<ol><li>You have to select from date.</li>
<li>You have to select to date.<br>
<b>Eg :</b>Suppose if you want to asses the client from "03.08.2011" to "12.09.2011" then you have to select<em style="color:#0066FF">03.08.2011</em> as "From Date", and <em style="color:#0066FF">12.09.2011</em> as To Date.</li>
<li>You have to select Unique Number it may be PAN(Perminent Account Number) or PASSPORT Number.<br>
<b>Eg :</b>Suppose if your Unique Number is "UID4537" then you have to select<em style="color:#0066FF">UID4537</em> as "Unique Number".</li>
<li>You have to select Support Type.<br>
<b>Eg :</b>Suppose if your are contacting with only one employee of the company then you have to select <em style="color:#0066FF">single</em> as "Support Type". or if your are contacting with morethan one employee of the company then you have to select <em style="color:#0066FF">multiple</em> as "Support Type". </li>

<li>If you select Support type as "Single", you have to select employee from the list box. <br>
If you select Support type as "Multiple", you have to select More than one employee from the list box(Hold control key to select multiple employees).</li>
<li>If you want to write something about assesment, write it in narration box.</li>

<li>Once filled click "SAVE" button to Save Assesment or click "CANCEL" button to exit without Saving Assesment.</li>
