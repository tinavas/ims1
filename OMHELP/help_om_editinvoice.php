<p><b>&nbsp;Invoice Generation</b></p>
<p>To Generate Invoice</p>
<ol><li>You have to select date.</li>
<li>You have to select Unique Number it may be PAN(Perminent Account Number) or PASSPORT Number.<br>
<b>Eg :</b>Suppose if  U.No is "UID4537" then you have to select<em style="color:#0066FF">UID4537</em> as "U.No".</li>

<li>You have to select client, to whom invoice is to be generate.<br>
<b>Eg :</b>Suppose if client "Steven" then you have to select <em style="color:#0066FF">Steven</em> as "client".</li>
<li>You have to enter Consultancy or Service Charges.<br>
<b>Eg :</b>Suppose if Consultancy or Service Charges are 5500, you have to enter<em style="color:#0066FF">5500</em> as "Consultancy / Service Charges".</li>

<li>You have to enter Other Charges.<br>
<b>Eg :</b>Suppose if Other Charges are 5500, you have to enter<em style="color:#0066FF">5500</em> as "Others".</li>

<li>You have to enter Discount Amount.<br>
<b>Eg :</b>Suppose if Discount Amount is 500, you have to enter<em style="color:#0066FF">500</em> as "Discount".</li>

<li>Net Amount will be displayed automatically while you enter discount Amount.</li>
<li>If you want to write something , write it in narration box.</li>

<li>Once filled click "SAVE" button to Save Invoice or click "CANCEL" button to exit without Saving Invoice.</li>
