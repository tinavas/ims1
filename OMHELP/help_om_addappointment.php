<p><b>&nbsp;Client Appointment</b></p>
<p>Add Client Appointment</p>
<ol><li>You have to select date, on which date appointment is giving.</li>
<li>You have to select employee to whom appointment is to be assign.<br>
<b>Eg :</b>Suppose if you want to assign to "Steven" you have to select <em style="color:#0066FF">Steven</em> as "Employee".</li>
<li>You have to select Unique Number it may be PAN(Perminent Account Number) or PASSPORT Number.<br>
<b>Eg :</b>Suppose if your U.No is "UID4537" then you have to select<em style="color:#0066FF">UID4537</em> as "U.No".</li>

<li>You have to select Appointment Date.<br>
<b>Eg :</b>Suppose if Appointment Date is "25.09.2011" then you have to select <em style="color:#0066FF">25.09.2011</em> as "Appointment Date".</li>
<li>You have to enter Time From and Time To.<br>
<b>Eg :</b>Suppose if Appointment is from "08:30" to "11:30" then you have to enter<em style="color:#0066FF">08:30</em> as "Time From", and <em style="color:#0066FF">11:30</em> as Time To.</li>

<li>If you want to write something about Appointment, write it in narration box.</li>

<li>Once filled click "SAVE" button to Save Client Appointment or click "CANCEL" button to exit without Saving Client Appointment.</li>
