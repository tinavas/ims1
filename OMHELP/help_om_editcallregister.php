<p><b>&nbsp;Call Register</b></p>
<p>Add Call Register</p>
<ol><li>You have to select date on which the call takes place.<br>
<b>Eg :</b>Suppose date is "25.09.2011" you have to select <em style="color:#0066FF">25.09.2011</em> for "Date".</li>
<li>You have to select employee who receive or make the call.<br>
<b>Eg :</b>Suppose if  "Steven" answer that call, you have to select <em style="color:#0066FF">Steven</em> as "Employee".</li>
<li>You have to select Unique Number it may be PAN(Perminent Account Number) or PASSPORT Number.<br>
<b>Eg :</b>Suppose if  U.No is "UID4537" then you have to select<em style="color:#0066FF">UID4537</em> as "U.No".</li>
<li>you have to enter dialled/out no.<br>
<b>Eg :</b>Suppose if  number is "9656764656" you have to enter <em style="color:#0066FF">9656764656</em> as "Dialled/Out No".</li>
<li> You have to select the call type, whether is it incoming call or out going call.</li>
<li>You have to enter call StartTime and End Time.<br>
<b>Eg :</b>Suppose if call duration is from "08:30:45" to "08:36:25" then you have to enter<em style="color:#0066FF">08:30:45</em> as "Start Time", and <em style="color:#0066FF">08:36:25</em> as End Time.</li>

<li>If you want to write something about Call, write it in narration box.</li>

<li>Once filled click "SAVE" button to Save Call Register or click "CANCEL" button to exit without Saving Call Register.</li>
