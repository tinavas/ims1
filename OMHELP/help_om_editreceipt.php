<p><b>&nbsp;Receipt</b></p>
<p>To Add Receipt</p>
<ol><li>You have to select date.</li>

<li>You have to select Unique Number it may be PAN(Perminent Account Number) or PASSPORT Number.<br>
<b>Eg :</b>Suppose if  U.No is "UID4537" then you have to select<em style="color:#0066FF">UID4537</em> as "U.No".</li>


<li>You have to select select Choice.<br>
<b>Eg :</b>Suppose if you paying amount On Account then you have to select <em style="color:#0066FF">On A/C</em> as "choice". (or)
if you paying for individual invoices then you have to select <em style="color:#0066FF">Invoice</em> as "choice".</li>

<li>You have to select Reception Mode.<br>
<b>Eg :</b>Suppose if amount received by cheque then you have to select <em style="color:#0066FF">Cheque</em> as "Reception Mode". (or) If you received cash then you have to select <em style="color:#0066FF">Cash</em> as "Reception Mode". </li>

<li>You have to select cash code or bank cccount number(based on the option you selected for Reception Mode, it will automatically give the options regarding it).<br>
<b>Eg :</b>Suppose if Cash Code is "CH" then you have to select <em style="color:#0066FF">CH</em> as "Cash Code".</li>
<li>If you selected choice as "On A/C". then you have to enter Amount,Bank,Branch,Cheque Number,Cheque Date(if Reception mode is Cheque then only cheque number and cheque date is required).
<b>Eg :</b>Suppose if Amount is "10000" Bank is "ICICI", Branch is "Bangalore", cheque number is "956847659" and cheque date is "19.09.2011" then you have to enter <em style="color:#0066FF">10000</em>For Amount,<em style="color:#0066FF">ICICI</em>for Bank,<em style="color:#0066FF">Bangalore</em> for Branck,<em style="color:#0066FF">956847659</em>for Cheque Number and <em style="color:#0066FF">19.09.2011</em> for Cheque date.</li> 

<li> If you selected Choice as "Invoice", then the list of invoices will displayed of particular customer. You have to enter received amount for each invoice.</li>

<li>If you want to write something about Reciept, write it in narration box.</li>

<li>Once filled click "SAVE" button to Save Receipt or click "CANCEL" button to exit without Saving Receipt.</li>
