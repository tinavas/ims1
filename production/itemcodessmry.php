	} elseif (@$_GET["cmd"] <> "") {
		$sCmd = $_GET["cmd"];
		if (strtolower($sCmd) == "reset") {
			ClearSessionSelection('cat');
			ClearSessionSelection('sunits');
			ClearSessionSelection('cunits');
			ResetPager();
		}
	}

	// Load selection criteria to array
	// Get Cat selected values

