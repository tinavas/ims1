	while ($rswrk && !$rswrk->EOF) {
		$x_cunits = $rswrk->fields[0];
		if (is_null($x_cunits)) {
			$bNullValue = TRUE;
		} elseif ($x_cunits == "") {
			$bEmptyValue = TRUE;
		} else {
			$t_cunits = $x_cunits;
			ewrpt_SetupDistinctValues($GLOBALS["val_cunits"], $x_cunits, $t_cunits, FALSE);
		}
		$rswrk->MoveNext();
