		ewrpt_SetupDistinctValues($GLOBALS["val_farmer"], EW_REPORT_EMPTY_VALUE, EW_REPORT_EMPTY_LABEL, FALSE);
	if ($bNullValue)
		ewrpt_SetupDistinctValues($GLOBALS["val_farmer"], EW_REPORT_NULL_VALUE, EW_REPORT_NULL_LABEL, FALSE);

