<ol><h3>Flock Initial Setting </h3>
<p><b>Pre-requisites : </b>"Item Codes" for Feed should be created.</p>
<li>Select the value for Unit.</li>
<li>Next Select the value for Shed</li>
<li>Next Select the value for Flock</li>
<li>Next Pick the date for Staring flock</li>
<li>Next Enter the value for Open Females and Open Males</li>
<li>Now enter value for Male Mort, Female Culls, B.Wt (Male Body weight), select value for Feed(Male Feed), enter value for Kg's (No of Kg's of Male Feed).</li>
<li>Enter values for E.Wt(Egg Weight), Water, Hatch Eggs, Table Eggs, Crack Eggs etc. in the appropriate boxes provided.</li>
 <br/>
<b> Eg : </b>Suppose if unit name is "SH",shed Name is "B3",Flock Name is "B3-S58", Start Date is "12th oct, 2011", Age will be "1", Female Mortality is "100", Female Culls is "0", Female Body Weight is "1220", Female Feed Type is "FD100"(whose description can be seen as Breeder Chick Mash on mouse over), Female Feed in Kg's is "380", Male Mortality is "100", Male Culls is "0", Male Body Weight is "1300", Male Feed Type is "FD106"(whose description can be seen as Male Mash on mouse over) and Male feed in Kg's is "32.8" then <br/>
You have to select <em style="color:#0066FF">SH</em> for "Unit".next select <em style="color:#0066FF">B3</em> for "shed".select <em style="color:#0066FF">12/10/2011</em> for "Start Date".Now enter the Open Female as <em style="color:#0066FF">100</em>.next enter Open Male<em style="color:#0066FF"> 20</em>
Now you can enter <em style="color:#0066FF">1</em> for "Mort"(Female Mortality), select <em style="color:#0066FF">FD100</em> for "Feed"(Female Feed), enter <em style="color:#0066FF">380</em> for "Kg's"(Female Feed in Kg's), select <em style="color:#0066FF">Fd106</em> for "Feed"(Male Feed) and enter <em style="color:#0066FF">32.8</em> for "Kg's"(Male Feed in Kg's) .
