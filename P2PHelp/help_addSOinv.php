<p><b>&nbsp;&nbsp;&nbsp;Add New Supplier Order Invoice</b></p>
<ol><b>To Add new Supplier Order Invoice</b>
<li>First You have to select the "Vendor", "Goods Receipt #" and "Date" from the dropdownlists.<br/>
As and when the "GR" value is selected the details such as "S.No", "Code", "Description", "Received Quantity", "Price", "Total Amount without Tax", "Total" and "Grand Total" values will be displayed.</li> 
<li>Now you can enter the values of "Book Invoice" and "Tax" then the "Grand Total" will be calculated as "Total - Tax" and displayed automatically.</li>
<br/>
 <b>Eg:</b> If suppose the GR "GR-1210-0002" is selected,then all the details of "GR-1210-0002" will be displayed.<br/>
 Now have to enter the value such as <em style="color:#0066FF">Inv1234</em> for "Invoice" and <em style="color:#0066FF">100</em> for "Tax".
 <li>Once filled click on 'SAVE' button to save the  Supplier Order Invoice or 'CANCEL' to exit without saving.</li>
</ol>
