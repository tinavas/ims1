<ol><b>To Create new Supplier</b>
<li>You have to enter the values "Name", "Address", "Place", "Phone", "Mobile", "PAN/TIN" and  "Note".</li>
<li>Select the values for "Contact Type" and "Supplier Group" from dropdowns.</li><br/>
 <b>Eg:</b> If suppose the Name of Supplier is "Arihanth Trading Co.", Address is "Bangalore", Phone Number is "08042111111", Mobile Number is "09010203040", Contact Type is "Vendor" and Supplier Group is "00458" you have to enter <em style="color:#0066FF">Arihanth Trading Co.</em> for "NAME" ,<em style="color:#0066FF">Bangalore</em> for "ADDRESS",<em style="color:#0066FF">08042111111</em> for "PHONE" and <em style="color:#0066FF">09010203040</em> for "MOBILE".<br/>
 Now select <em style="color:#0066FF">Vendor</em> for "CONTACT TYPE" and <em style="color:#0066FF">00458</em> for "SUPPLIER GROUP" .
 <li>Once filled click on 'SAVE' button to save or 'CANCEL' to exit.</li>
