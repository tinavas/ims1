<p><b>&nbsp;&nbsp;&nbsp;Edit Existing Payment</b></p>
<ol><b><p>To edit an existing Payment</p></b></ol>
<ol>
<li>Select the "Date" of Payment, "Vendor", "Payment Method" and "TDS" if any changes from previosly selected values required.</li>
<li>If the selected value of "Payment Method" is "Pre Payment", 
<ol>Select the value for "Choice" as either "POs" or "ON A/C".</ol>
<ol>If the selected value of "Choice" is "POs" you have select the value for "PO" and enter the value for "Amount Paid".</ol>
OR<br/>
If the selected value of "Payment Method" is "Payment",
<ol>Select the value for "Choice" as "SOBIs" or "Credit Notes" or "ON A/C".</ol>
<ol>If the selected value of "Choice" is "SOBIs" you have select the value for "SOBI" and enter the value for "Amount Paid".</ol>
</li> 
<li>Select values for "Payment Mode".</li>
<li>If the selected value for "Payment Mode" is CASH
<ol>You have to select the value for "Cash Code". </ol>
<ol>As and when the Cash Code value is selected the "Code" and "Description" values will be displayed automatically.</ol>
<ol>Now you have to enter the value of "Amount".</ol>
OR<br/>
If the selected value for "Payment Mode" is CHEQUE
<ol>You have to select the value for "Bank A/C No".</ol>
<ol>As and when the Bank A/C No. value is selected the "Code" and "Description" values will be displayed automatically.</ol>
<ol>Now you have to enter the value of "Amount", "Cheque #" and "Cheque Date".</ol>
</li>
<li>Now enter "Amount", "Cheque #" and "Cheque Date" if you want to change the previously entered values.</li>
<li>If the selected value of "TDS" is WITH TDS,
<ol>You have to select the value for "Code" and enter the value for "Amount" until the values for all the Payments are filled.</ol></li>
<br/>
<b>Eg :</b>Suppose if you want to change the payment Date from "1st Jan, 2011" to "06 Jan, 2011" and Vendor from "Rajesh" to "MAHESH" then <br/> 
you have to select <em style="color:#0066FF">06.01.2011</em> for "Date" and select <em style="color:#0066FF">Mahesh</em> for "Vendor". 
<li>Once filled click "PAY" button to Save changes to the selected Payment or click "CANCEL" button to exit without Saving the changes to the selected Payment.</ol></div><!--end of procuretoppay_Pro_addpayment-->
