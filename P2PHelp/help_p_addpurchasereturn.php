<p><b>&nbsp;&nbsp;&nbsp;Add New Purchase Return </b></p>
<ol><b>To Add New Purchase Return</b>
<li>You have to select the values for "Vendor Name", "SOBI" and  "Date".<br/>
As and when the value of SOBI is selected the details such as "Item Code", "Description", "Purchased Quantity" and "Amount" will be displayed automatically.</li>
<li>Now enter the value of "Returned Quantity" and select value for "Goods Status" for all the purchased items.</li><br/>
 <b>Eg:</b> If suppose you want to enter the Returned Quantity(which you want to remove from stock) of "10" to Item Code "BROC101" for SOBI "SOBI-0611-0015" of Vendor "SRI SAI" on "15th June, 2011" <br/>
you have to select <em style="color:#0066FF">SRI SAI</em> for "Vendor", <em style="color:#0066FF">SOBI-0611-0015</em> for "SOBI", <em style="color:#0066FF">15.06.2011</em> for "Date".<br/>
Now enter <em style="color:#0066FF">10</em> for "Returned Quantity"(of item code BROC101) and select <em style="color:#0066FF">Remove From Stock</em> for "Goods Status".<br/><br/>
  <li>Once filled click on "SAVE" button to save purchase return or "CANCEL" button to exit with out saving the purchase return.</li>
</ol>

