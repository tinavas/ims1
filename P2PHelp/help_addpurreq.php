<p><b>&nbsp;&nbsp;&nbsp;Add New Purchase Request </b></p>
<ol><b>To Add new Purchase Request</b>
<li>You have to select the value of "Date".</li>
<li>select values for "Category" and "Item Code" from dropdowns provided.<br/>
The "Description" and "Units" will be displayed automatically as and when the value of Item Code is selected.</li>
<li>Now enter the the value of "Quantity" requested. </li>
<li>Select values for "Req. Date", "Delivery Office", "Receiver" and "QC".</li><br/>

 <b>Eg:</b> If suppose the Purchase Request Date is "03.01.2011", Category is "Ingredients", Item Code is "MZ101", Quantity is "100", Delivery Office is "Banglore office", Receiver is "Ganesh" and  QC is "No"  you have to select <em style="color:#0066FF">03.01.2011</em> for "REQ. DATE" ,<em style="color:#0066FF">Ingredients</em> for "CATEGORY" and <em style="color:#0066FF">MZ101</em> for "ITEM CODE".<br/>The value of "DESCRIPTION"
is automatically displayed as <em style="color:#0066FF">Maize</em> and "UNITS" will be displayed as  <em style="color:#0066FF">Kgs</em> as and when the Item Code is selected.<br/>
 Now enter <em style="color:#0066FF">100</em> for "QUANTITY" ,select <em style="color:#0066FF">Banglore office</em> for "DELIVERY OFFICE" , <em style="color:#0066FF">Ganesh</em> for "RECEIVER" and <em style="color:#0066FF">No</em> for "QC".
 <li>Repeat the above steps 2,3 and 4 until required details are filled.</li>
 <li>Once filled click on 'SAVE' button to save the Purchase request or 'CANCEL' to exit with out saving the Purchase Request.</li>
</ol>
