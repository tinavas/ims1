<p><b>&nbsp;&nbsp;&nbsp;Edit existing Purchase Request </b></p>
<ol><b>To Edit existing Purchase Request</b>
<li>You have to select the values of "Date", "Category" and "Item Code" from dropdowns if any changes from previous values.<br/>
The "Description", "Units" and "Req. Date" will be displayed automatically.</li>
<li>Now enter the the value of "Quantity" requested </li>
<li>Select values for "Delivery Office", "Receiver" and "QC".</li><br/>

 <b>Eg:</b> If suppose you want to change the Delivery Office from "Banglore office" to "Sujay FeedMill" and Receiver from "Ganesh" to "Abhishek" you have to select <em style="color:#0066FF">Sujay FeedMill</em> for "DELIVERY OFFICE" and <em style="color:#0066FF">Abhishek</em> for "RECEIVER".
 
 <li>Once filled click on 'UPDATE' button to save the changes to the Purchase request or 'CANCEL' to exit with out saving the the changes.</li>
</ol>
