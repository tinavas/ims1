<p><b>&nbsp;&nbsp;&nbsp;Add New Purchase Order</b></p>
<ol><b>To Add new Purchase Order</b>
<li>First You have select "With Purchase Request" or With Out Purchase Request"</li>
<li>Select values for "Vendor", "Broker", "Date"(purchase Order Date) and "Credit Term" values from the dropdownlists provided.<br/>
<b>Eg : </b>Suppose Vendor is "A B M Industries", Broker is "Muniram", Purchase Order Date is "31.12.2010" and Credit Term is "Immediate" then <br/> you have to select <em style="color:#0066FF">A B M Industries</em> for "Vendor", <em style="color:#0066FF">Muniram</em> for "Broker", <em style="color:#0066FF">31.12.2010</em> for "Date" and <em style="color:#0066FF">Immediate</em> for "Credit Term".</li>

<p style="text-decoration:underline">Click on Purchase Order</p>
<li>You have to select the values of "Category", "Item Code", "Delivery Date" and "PR #" if "With Purchase Request".<br/>
Enter the values of "Rate/Unit" and "Quantity" if "With Out Purchase Request".<br/>

  <b>Eg :</b>Suppose Category is "Ingredients", Item code is "MZ101", PR # is "PR-1210-0004", Rate/Unit is "12" and Delivery Date is "05.01.2011" you have to select <em style="color:#0066FF">Ingredients</em> for "Category", <em style="color:#0066FF">MZ101</em> for "Item Code", <em style="color:#0066FF">PR-1210-0004</em> for "PR #", <em style="color:#0066FF">05.01.2011</em> for "Delivery Date" and enter <em style="color:#0066FF">12</em> for "Rate/Unit".</li>
 <p style="text-decoration:underline">Click on Delivery Details</p>
<li>You have to select  "D.Location"(Delivery Location) and  "Receiver" from dropdowns.<br/>
 <b>Eg :</b>Suppose Delivery Location is "banglore Office" and Receiver is "Ganesh" you have to select <em style="color:#0066FF">Banglore Office</em> for "D. Location" and <em style="color:#0066FF">Ganesh</em> for "Receiver".</li>
 <p style="text-decoration:underline">Click on Taxes</p> 
 <li>You can select "Tax" select the dropdown provided.<br/>
 <b>Eg :</b>Suppose if Tax is "VAT1P" you have to select <em style="color:#0066FF">VAT1P</em> for "Tax".</li>
 <p style="text-decoration:underline">Click on Charges</p>
 <li>Select "Frieght" and "Brokerage" from dropdowns.<br/>
 <b>Eg : </b>Suppose Frieght is "FR001" and Brokerage is "BR001" you have to select <em style="color:#0066FF">FR001</em> for "Frieght" and <em style="color:#0066FF">BR001</em> for "Brokerage".</li>
 <p style="text-decoration:underline">Click on Discounts</p>
 <li>Select "Discount" from the dropdown provided.<br/>
 <b> Eg : </b>Suppose Discount is "SD001" you have to select <em style="color:#0066FF">SD001</em> for "Discount" from the dropdownlist.</li>
 <p style="text-decoration:underline">Click om Terms & Conditions</p>
 <li>Select appropriate values of Terms and Conditions from the select box provided.<br/>
 As and when the value is selected the description will be displayed automatically.<br/>
 <b>Eg : </b>Suppose "Frieght 50" and "CFORM" are the Terms & Conditions then you have to select both <em style="color:#0066FF">Frieght 50</em> and <em style="color:#0066FF">CFORM</em> from Select Box then the Description Such as <em style="color:#0066FF">50 % Freight will be given</em> will be displayed automatically.
</li> 
 <li>Once filled click on 'SAVE' button to save the Purchase Order or 'CANCEL' to exit without saving.</li>
</ol>

