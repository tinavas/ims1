<p><b>&nbsp;&nbsp;&nbsp;Add New Tax Master</b></p>
<ol><b>To Add new Tax Master</b>
<li>You have to enter the values of "Tax Code" and "Description"</li> 
<li>You have to select the value of "Deductions" , if it is "Charges" you have to select the value of "Charge Type" also.</li>
<li>Now select the value for "Rule", if it is "Exclude" you have to selct the value of "CoA".</li>
<li>Select either "Formula" or "Flat" by selecting the appropriate radio button and enter the values of "Tax Code Value" and "Formula ".<br/>
    <b>Eg :</b>Suppose Tax Code is "tax001", Description is "taxcode001", Deductions is "Charges", Charge Type is "Brokerage", Rule is "Exclude", CoA is "IT401", Tax Code Value is "1.5" and Formula is "A+(A * tax001/100)" then,<br/>
  you have to enter <em style="color:#0066FF">tax001</em> for "Tax Code", <em style="color:#0066FF">taxcode001</em> for "Description", select <em style="color:#0066FF">Charges</em> for "Deductions", <em style="color:#0066FF">Brokerage</em> for "Charge Type", <em style="color:#0066FF">Exclude</em> for "Rule", <em style="color:#0066FF">IT401</em> for "CoA".<br/>
 Now enter <em style="color:#0066FF">1.5</em> for "Tax Code Value" and <em style="color:#0066FF">"A+(A * tax001/100)</em> for "Formula". </li>
<li>Once filled click on 'SAVE' button to save the Tax Master or 'CANCEL' to exit without saving.</li>
</ol>
