<p><b>&nbsp;&nbsp;&nbsp;Add New Payment</b></p>
<ol><b><p>To Add a New Payment</p></b></ol>
<ol>
<li>Select the "Date" of Payment, "Farmer", "Flock".</li>

<li>Select values for "Payment Mode".</li>
<li>If the selected value for "Payment Mode" is CASH
<ol>You have to select the value for "Cash Code". </ol>
<ol>As and when the Cash Code value is selected the "Code" and "Description" values will be displayed automatically.</ol>
<ol>Now you have to enter the value of "Amount".</ol>
OR<br/>
If the selected value for "Payment Mode" is CHEQUE
<ol>You have to select the value for "Bank A/C No".</ol>
<ol>As and when the Bank A/C No. value is selected the "Code" and "Description" values will be displayed automatically.</ol>
<ol>Now you have to enter the value of "Amount", "Cheque #" and "Cheque Date".</ol>
<li>Now enter "Amount", "Cheque #" and "Cheque Date".</li>


<b>Eg :</b>Suppose the payment Date is "07 May, 2012", Farmer is "Chroek Moeun", Flock is "Chroek Moeun2",Payment Mode is "Cheque", Bank Code is "Acilida Bank", Code is "AS106", Description is "Acilida Bank", Amount is "12500",<br/> 
you have to select <em style="color:#0066FF">07.05.2012</em> for "Date", <em style="color:#0066FF">Chroek Moeun</em> for "Farmer", <em style="color:#0066FF">Chroek Moeun2</em> for "Flock",<em style="color:#0066FF">Cheque</em> for "Payment Mode", <em style="color:#0066FF">Acilida Bank</em> for "Bank Code". As and when the Bank Code value is selected the value of "Code" will be displayed as <em style="color:#0066FF">AS106</em> and "Description" is displayed as <em style="color:#0066FF">Acilida Bank</em>.<br/>
Now enter <em style="color:#0066FF">12500</em> for "Amount",<em style="color:#0066FF">6589472</em> for "Cheque" and <em style="color:#0066FF">05.05.2012</em> for Cheque Date.</li>
<li>Once filled click "PAY" button to Save the Payment or click "CANCEL" button to exit without Saving the Payment.</ol></div><!--end of procuretoppay_Pro_addpayment-->
