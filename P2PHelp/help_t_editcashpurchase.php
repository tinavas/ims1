<p><b>&nbsp;&nbsp;&nbsp;Edit Existing Cash Purchase</b></p>
<ol><b>To Edit Existing Cash Purchase</b>
<li>You have to select the "Date" of the Purchase, "Vendor", and enter the value of "Book Invoice"(if not available make it "0"(Zero) ) if any changes from previous values required.<br/>
The "Invoice" value will be generated automatically based on which Payments will be made</li><br/>
<b>Eg:</b> If suppose for the Invoice "SOBI-0111-0020" you want to change the Purchase Date from "1st Jan, 2011"  to "5th Jan, 2011" <br/>
You have to select <em style="color:#0066FF">05.01.2011</em> for Date. 
  <li>You can select "Category" and "Code".<br/>As and when the "Code" value is selected the "Description" and "Units" of the selected item will be displayed automatically.</li>
<li>Now enter the "Quantity Sent"(By Vendor ), "Quantity Received", (No of )"Bags" and "Price/Unit" if you want any changes from previous values.</li>
<li>Select the values for "Type"(of Bags) and "VAT"( "None" if no VAT ).<br/>The "Basic Amount" will be calculated based on the "Quantity Received" and "VAT" and displayed in the appropriate box given.</li><br/>
<li>Repeat the steps 2 to 6 until the information of all Purchased items is filled.</li> 
<li>To enter the Discount in % select "%" Radio button or if you want to enter the Discount Amount select the "Amt" Radio button and enter the appropriate "Discount" value in appropriate input box provided.<br/>
The "Total Price" will be calculated automatically based on the "Discount" and the "Basic Price".</li><br/>
<li>Enter values for "Vehicle No.", "Driver Name" and select appropriate value for "Broker Name" from the dropdown provided if you want any changes from previous values.</li><br/>
<li>Select value for "Freight" as Included( in the Price/Unit) or Excluded , Account Code and enter "Freight Amount".<br/>
Based on "Total Price" and "Freight Amount" the value of "Grand Total" will be calculated.</li>
<li>You have to select the Payment Mode for Freight from dropdown named "Via" such as CASH, CHEQUE or OTHERS.<br/>
If it is CASH you have to select "Cash Code" and value for "Dated"<br/>
If it is CHEQUE you have to select values for "Bank A/C No", "Dated" and enter value for "Cheque"(Cheque Number).  </li><br/>

<li>Once filled click on 'SAVE' button to save changes to the selected Cash Purchase or 'CANCEL' to exit without saving the changes to the selected Cash Purchase.</li>
</ol>

