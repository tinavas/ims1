<p><b>&nbsp;&nbsp;&nbsp;Edit existing Credit Note</b></p>
<ol><b>To Edit existing Credit Note</b>
<li>You can select the value of "Vendor" if any change in the previously selected Vendor value by selecting it from the dropdownlist.</li>
<li>You can enter the values of "amount" and "Dr" or "Cr" depending on the selected value of "Dr/Cr" value.</li> <br/>

<b>Eg :</b>Suppose if you want to change the Vendor from "Goutham Trading Co." to "Nandhini Traders" and Date from "31.12.2010" to "01.01.2011" you have to select <em style="color:#0066FF">Nandhini Traders</em> for "Vendor" and <em style="color:#0066FF">01.01.2011</em> for "Date".<br/>
 <li>Once filled click on 'UPDATE' button to save the changes to Credit Note or 'CANCEL' to exit without saving.</li>
</ol>
