<p><b>&nbsp;&nbsp;&nbsp;Add New Vendor Group Mapping </b></p>
<ol><b>To Add new Vendor Group Mapping</b>
<li>You have to enter the values for "Vendor Group Code", "Vendor Group Description" and  "Currency ".</li>
<li>Select the values for "Vendor Controll A/C" and "Vendor Prepayment A/C" from dropdowns.</li><br/>
 <b>Eg:</b> If suppose the Vendor Group Code is "00345", Vendor Group Description is "State Vendors Group", Currency is "Rs", Vendor Controll A/C is "VA505" and  Vendor Prepayment A/C is "VP500"  you have to enter <em style="color:#0066FF">00345</em> for "Vendor Group Code" ,<em style="color:#0066FF">State Vendors Group</em> for "Vendor Group Description" and <em style="color:#0066FF">Rs</em> for "Currency".<br/>
 Now select <em style="color:#0066FF">VA505</em> for "Vendor Controll A/C" and <em style="color:#0066FF">VP500</em> for "Vendor Prepayment A/C" .
 <li>Once filled click on 'SAVE' button to save or 'CANCEL' to exit.</li>
