<p><b>&nbsp;&nbsp;&nbsp;Edit existing Tax Master</b></p>
<ol><b>To Edit existing Tax Master</b>
<li>You have to enter the values of "Tax Code" and "Description" if any changes in previously entered values are required.</li> 
<li>You have to select the value of "Deductions" , if it is "Charges" you have to select the value of "Charge Type" also.</li>
<li>Now select the value for "Rule", if it is "Exclude" you have to select the value of "CoA".</li>
<li>Select either "Formula" or "Flat" by selecting the appropriate radio button and enter the values of "Tax Code Value" and "Formula ".<br/>
    <b>Eg :</b>Suppose if you want to change Tax Code from "tax001" to "tax0011,  Deductions from "Charges" to "Discount" then,<br/>
  you have to enter <em style="color:#0066FF">tax0011</em> for "Tax Code" and select <em style="color:#0066FF">Discount</em> for "Deductions".</li>
 
<li>Once filled click on 'UPDATE' button to save the changes to Tax Master or 'CANCEL' to exit without saving.</li>
</ol>
