<p><b>&nbsp;&nbsp;&nbsp;Add New Receipt</b></p>
<ol><b><p>To Add a New Receipt</p></b></ol>
<ol>
<li>Select the "Date" of Receipt, "Vendor", "Receipt Method", "TDS",  "Choice" and "Receipt Mode".</li>
<li>If the selected value for "Receipt Mode" is CASH
<ol>You have to select the value for "Cash Code". </ol>
<ol>As and when the Cash Code value is selected the "Code" and "Description" values will be displayed automatically.</ol>
<ol>Now you have to enter the value of "Amount".</ol>
OR<br/>
If the selected value for "Receipt Mode" is CHEQUE
<ol>You have to select the value for "Bank A/C No".</ol>
<ol>As and when the Bank A/C No. value is selected the "Code" and "Description" values will be displayed automatically.</ol>
<ol>Now you have to enter the value of "Amount", "Cheque #" and "Cheque Date".</ol>

<li>If the selected value of "TDS" is WITH TDS,
<ol>You have to select the value for "Code" and enter the value for "Amount" until the values for all the Receipts are filled.</ol></li>
<b>Eg :</b>Suppose the Receipt Date is "06 Jan, 2011", Vendor is "MAHESH", Receipt Method is "Receipt", TDS is "Without TDS", Choice is "On A/C", Receipt Mode is "Cash", Cash Code is "CIH", Code is "AS111", Description is "1.CASH", Amount is "5000" then,<br/> 
you have to select <em style="color:#0066FF">06.01.2011</em> for "Date", <em style="color:#0066FF">MAHESH</em> for "Vendor", <em style="color:#0066FF">Receipt</em> for "Receipt Method", <em style="color:#0066FF">Without TDS</em> for "TDS", <em style="color:#0066FF">SOBIs</em> for "Choice", <em style="color:#0066FF">Cash</em> for "Receipt Mode", <em style="color:#0066FF">CIH</em> for "Cash Code". As and when the Cash Code value is selected the value of "Code" will be displayed as <em style="color:#0066FF">AS111</em> and "Description" is displayed as <em style="color:#0066FF">1.CASH</em>.<br/>
Now enter <em style="color:#0066FF">4000</em> for "Amount"</li>
<li>Once filled click "PAY" button to Save the Receipt or click "CANCEL" button to exit without Saving the Receipt.</ol>
