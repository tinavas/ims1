<ol><b>To Edit existing Vendor Group Mapping</b>
<li>You have to enter the values for "Vendor Group Code", "Vendor Group Description" and  "Currency " if there is any change in their previous values.</li>
<li>Select the values for "Vendor Controll A/C" and "Vendor Prepayment A/C" from dropdowns.</li><br/>
 <b>Eg:</b> If suppose you want to change the Vendor Group Code from "00458" to "12300", Currency from "Rs" to "$" and  Vendor Prepayment A/C from "VP500" to "VP550"   you have to enter <em style="color:#0066FF">12300</em> for "Vendor Group Code" and <em style="color:#0066FF">$</em> for "Currency".<br/>
 Now select <em style="color:#0066FF">VP500</em> for "Vendor Prepayment A/C" .
 <li>Once filled click on 'UPDATE' button to save the changes or 'CANCEL' to exit with out saving.</li>
