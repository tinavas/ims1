<p><b>&nbsp;&nbsp;&nbsp;Add New Gate Entry</b></p>
<ol><b>To Add New Gate Entry</b>
<li>You have to select the values of "Date", "Vendor" from dropdowns provided and  enter "Vehicle #".</li>
<li>Now select the values for "Item Code", "PO #s" and enter value for "Quantity" in the appropriate input boxes provided.</li>
<li>Select value for "QC".</li><br/>
 <b>Eg:</b> If suppose the Date is "31.12.2010", Vendor is "Grams Lab India", Vehicle # is "KA25G2345", PO # is "PO-1210-001", Item Code is "MG101", Quantity is "1000", and  QC is "No" then <br/>you have to select <em style="color:#0066FF">31.12.2010</em> for "Date" , <em style="color:#0066FF">Grams Lab India</em> for "Vendor", enter <em style="color:#0066FF">KA25G2345</em> for "Vehicle #".<br/>
 Select <em style="color:#0066FF">MG101</em> for "Item Code", <em style="color:#0066FF">PO-1210-001</em> from "PO #s" enter <em style="color:#0066FF">1000</em> for "Quantity"  and  select <em style="color:#0066FF">No</em> for "QC".<br/>
<li>Repeat above step 3 until required details are filled.</li> 
 <li>Once filled click on 'SAVE' button to save the Gate Entry or 'CANCEL' to exit without saving.</li>
</ol>
