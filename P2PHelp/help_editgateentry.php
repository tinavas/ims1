<p><b>&nbsp;&nbsp;&nbsp;Edit existing Gate Entry</b></p>
<ol><b>To Edit existing Gate Entry</b>
<li>You have to select the values of "Date", "Choice", "Item Code","Vendor", "PO #" and  "QC" from dropdowns if any changes from the previous values.</li>
<li>You can Select select Multiple values from the selectbox for "PO #" if the selected value of "Choice" is <em style="color:#0066FF">Mutiple</em> ,if it is <em style="color:#0066FF">Single</em> you have to select single value.</li> 
<li>Now enter the values for "Vehicle #" and "Weight" in the appropriate boxes provided.</li><br/>

 <b>Eg:</b> If suppose you want to change the Vendor from "Grams Lab India" to "G J Bhora" and  Vehicle # from "KA25G2345" to "MH04G6778" then you have to select <em style="color:#0066FF">G J Bhora</em> for "Vendor" and enter <em style="color:#0066FF">MH04G6778</em> for "Vehicle #" .
 <li>Once filled click on 'UPDATE' button to save changes to the Gate Entry or 'CANCEL' to exit without saving.</li>
</ol>
