<p><b>&nbsp;&nbsp;&nbsp;Add New Customer Group Mapping</b></p>
<ol><b>To Add new Customer Group Mapping</b>
<li>You have to enter the values for "Customer Group Code", "Customer Group Description" and  "Currency".<br/>
<b>Eg :</b>Suppose Customer Group Code is "47778" , Customer Group Description is "State Customers" and Currency is "Rs" you have to enter <em style="color:#0066FF">47778</em> for "Customer Group Code", <em style="color:#0066FF">State Customers</em> for " Customer Group Description" and <em style="color:#0066FF">Rs</em> for "Currency".</li>
<li>Now select the values for "Customer Controll A/C"and "Customer Advance A/C".<br/>
 <b>Eg:</b> If suppose Customer Controll A/C is "45668" and "Customer Advance A/C" is "99896" you have to select <em style="color:#0066FF">45668</em> for "Customer Controll A/C" and <em style="color:#0066FF">99896</em> for "Customer Advance A/C".</li>
 <li>Once filled click on 'SAVE' button to save the Customer Group Mapping or 'CANCEL' to exit without saving.</li>
</ol>
