<p><b>&nbsp;&nbsp;&nbsp;Add New Customer</b></p>
<ol><b>To Add new Customer</b>
<li>You have to enter the values of "Name", "Address", "Place", "Phone", "Mobile" and "PAN/TIN" in the appropriate boxes.</li>
<li>Now you have to select the values for "Contact Type" and "Customer Group" .<br/>    
<b>Eg :</b>Suppose Name is "Abhiman P/F" , Address is "Bangalore", Phone is "08055698746", Mobile is "9141414223", Contact Type is "Customer&Supplier" and Customer Group is "47778" <br/> you have to enter <em style="color:#0066FF">Abhiman P/F</em> for "Name", <em style="color:#0066FF">Bangalore</em> for "Address", <em style="color:#0066FF">08055698746</em> for "Phone" and <em style="color:#0066FF">9141414223</em> for "Mobile".<br/>
 You have to select <em style="color:#0066FF">Customer&Supplier</em> for "Contact Type" and <em style="color:#0066FF">47778</em> for "Customer Group". </li>
<li>You can write any Notes in the Textbox Provided.</li>
 <li>Once filled click on 'SAVE' button to save the Customer or 'CANCEL' to exit without saving.</li>
</ol>
 
