<p><b>&nbsp;&nbsp;&nbsp;Edit existing  Customer Group Mapping</b></p>
<ol><b>To Edit existing  Customer Group Mapping</b>
<li>You have to enter the values for "Customer Group Code", "Customer Group Description" and  "Currency" if any changes in previously entered values.<br/>
<b>Eg :</b>Suppose if you want to change Customer Group Code from "47778" to "51234" and Currency from "Rs" to "$" you have to enter <em style="color:#0066FF">51234</em> for "Customer Group Code" and <em style="color:#0066FF">$</em> for "Currency".</li>
<li>Now select the values for "Customer Controll A/C" and "Customer Advance A/C" if any changes in previously selected values.<br/>
 <b>Eg:</b> If suppose you want to change the Customer Controll A/C from "45668" to "45779" you have to select <em style="color:#0066FF">45779</em> for "Customer Controll A/C".</li>
 <li>Once filled click on 'UPDATE' button to save the changes to Customer Group Mapping or 'CANCEL' to exit without saving.</li>
</ol>
 
