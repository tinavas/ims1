<p><b>&nbsp;&nbsp;&nbsp;Add New Sales Order</b></p>
<ol><b>To Add new Sales Order</b>
<li>Select "Vendor", "Broker" and "Date"(purchase Order Date) values from the dropdownlists provided.<br/>
<b>Eg : </b>Suppose Vendor is "AP PF" and Broker is "Muniram" and Sales Order Date is "31.12.2010" select <em style="color:#0066FF">AP PF</em> for "Vendor", <em style="color:#0066FF">Muniram</em> for "Broker" and <em style="color:#0066FF">31.12.2010</em> for "Date".</li>

<p style="text-decoration:underline">Click on Sales Order</p>
<li>You have to select the values of "Category", "Item Code" and "Delivery Date".<br/>
Enter the values of "Rate/Unit" and "Quantity".<br/>

  <b>Eg :</b>Suppose Category is "Birds", Item code is "BB101", Rate/Unit is "12", Quantity is "1000" and Delivery Date is "05.01.2011" you have to select <em style="color:#0066FF">Birds</em> for "Category", <em style="color:#0066FF">BB101</em> for "Item Code", <em style="color:#0066FF">05.01.2011</em> for "Delivery Date" and enter <em style="color:#0066FF">12</em> for "Rate/Unit", <em style="color:#0066FF">1000</em> for "Quantity".</li>
  <p style="text-decoration:underline">Click on Taxes</p> 
 <li>You can select "Tax" select the dropdown provided.<br/>
 <b>Eg :</b>Suppose if Tax is "VAT1P" you have to select <em style="color:#0066FF">VAT1P</em> for "Tax".</li>
 <p style="text-decoration:underline">Click on Charges</p>
 <li>Select "Frieght" and "Brokerage" from dropdowns.<br/>
 <b>Eg : </b>Suppose Frieght is "FR001" and Brokerage is "BR001" you have to select <em style="color:#0066FF">FR001</em> for "Frieght" and <em style="color:#0066FF">BR001</em> for "Brokerage".</li>
 <p style="text-decoration:underline">Click on Discounts</p>
 <li>Select "Discount" from the dropdown provided.<br/>
 <b> Eg : </b>Suppose Discount is "SD001" you have to select <em style="color:#0066FF">SD001</em> for "Discount" from the dropdownlist.</li>
 <p style="text-decoration:underline">Click om Terms & Conditions</p>
 <li>Select appropriate values of Terms and Conditions from the select box provided.<br/>
 As and when the value is selected the description will be displayed automatically.<br/>
 <b>Eg : </b>Suppose "Frieght 50" and "CFORM" are the Terms & Conditions then you have to select both <em style="color:#0066FF">Frieght 50</em> and <em style="color:#0066FF">CFORM</em> from Select Box then the Description Such as <em style="color:#0066FF">50 % Freight will be given<br/>
Cform to be sent along with the items</em> will be displayed automatically.
</li> 
 <li>Once filled click on 'SAVE' button to save the Sales Order or 'CANCEL' to exit without saving.</li>
</ol>
<b> NOTE : </b>To Add New "Tax","Charges" (Such as Freight and Brokerage) and "Discount"  add it using the <b>Tax Masters</b> available in <b>Masters</b> of <b>Inventory Management System</b>.<a href="http://localhost/bimstrail%20online/dashboard.php?page=addtaxmasters">Click here to Add New Tax or Frieght or Brokerage or Discount</a> <br/>
