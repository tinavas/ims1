<p><b>&nbsp;&nbsp;&nbsp;Add New Customer Order Invoice</b></p>
<ol><b>To Add new Customer Order Invoice</b>
<li>You have to select the values of "Party", "Pack Slip #" and  "Date".<br/>
As and when the "Pack Slip #" value is selected the details of Customer Order Invoice such as "Invoice #", "Item", "Description" "S.No  Item  Description  Sent Quantity","Total" and "Grand Total" of Items will be displayed automatically. 
<b>Eg :</b>Suppose Party is "Banglore(Breeder)" , Pack Slip # is "PS-1210-0003" and Date is "04 Dec,2010" you have to select <em style="color:#0066FF">Banglore(Breeder)</em> for "Party", <em style="color:#0066FF">PS-1210-0003</em> for "Pack Slip #" and <em style="color:#0066FF">04.12.2010</em> for "Date".<br/>
Suppose if there are 2 items present as and when the Pack Slip # is selected the details of the two items will be displayed. </li>
<li>Now you can enter the value for "Book Invoice #" and select "CoA Code" for all Items.<br/>
 <b>Eg:</b> If suppose if Book Invoice # is "invcobi111" , CoA Code of first item is "45688" and second item is "00502" you have to enter <em style="color:#0066FF">invcobi111</em> for "Invoice #", select <em style="color:#0066FF">45688</em> for "CoA Code" of first item and <em style="color:#0066FF">00502</em> for "CoA Code" of second item.</li>
 <li>Once filled click on 'SAVE' button to save the Customer Order Invoice or 'CANCEL' to exit without saving.</li>
</ol>
