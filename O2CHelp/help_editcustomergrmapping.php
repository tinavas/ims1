<li>Now select the values for "Customer Controll A/C"and "Customer Advance A/C" if any changes in previously selected values.<br/>
 <b>Eg:</b> If suppose you want to change the Customer Controll A/C from "45668" to "45779" you have to select <em style="color:#0066FF">45779</em> for "Customer Controll A/C".</li>
