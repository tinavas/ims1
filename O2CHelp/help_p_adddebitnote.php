<p><b>&nbsp;&nbsp;&nbsp;Add New Debit Note</b></p>
<ol><b>To Add new Debit Note</b>
<li>You have to select the values of "Date", "Customer" and  enter "Amount"(Debit Amount = Total of Cr. - Total of Dr.).<br/>
<b>Eg :</b>Suppose Date is "31 Dec, 2010" , Customer is "Goutham Trading Co." and Amount is "100" you have to select <em style="color:#0066FF">31.12.2010</em> for "Date", <em style="color:#0066FF">Goutham Trading Co.</em> for "Customer" and enter <em style="color:#0066FF">100</em> for "Amount".</li>
<li>Now select the values for "Code", "Cr/Dr" and enter the value of "Cr" or "Dr" depending on the select value of "Cr/Dr".</li>
<li>Repeat the above step any number of times you want(Until all the entries are filled).<br/></li>
 <b>Eg:</b> If suppose you want to enter 2 entries with code "00104" with Cr value of "500" and  Code "00502" with Dr value of "400" respectively ,<br/>
 You have to select <em style="color:#0066FF">00104</em> for "Code", <em style="color:#0066FF">Cr</em> for "Dr/Cr" and <em style="color:#0066FF">500</em> for "Cr" for the first entry and <br/>select <em style="color:#0066FF">00502</em> for "Code", <em style="color:#0066FF">Dr</em> for "Dr/Cr" and <em style="color:#0066FF">400</em> for "Dr" for second entry.
 <li>You can enter "Narration" in the Textbox provided.</li>
 <li>Once filled click on 'SAVE' button to save the Debit Note or 'CANCEL' to exit without saving the Debit Note.</li>
</ol>
<b> NOTE : </b> You can Save the Debit Note only when the the value of Total Of Cr. - Total of Dr. = Amount .
