<p><b>&nbsp;&nbsp;&nbsp;Add New Sales Return</b></p>
<ol><b>To Add New Sales Return</b>
<li>You have to select the values for "Party Name", "COBI" and  "Date".<br/>
As and when the value of COBI is selected the details such as "Item Code", "Item Name" and "Sold Quantity"  will be displayed automatically.</li>
<li>Now enter the value of "Returned Quantity" and select value for "Goods Status" for all the purchased items.</li><br/>
 <b>Eg:</b> If suppose you want to enter the Returned Quantity(which you want to add to stock) of "10" to Item Code "BROC101" for SOBI "COBI-0611-0015" of Party "SRI SAI" on "15th June, 2011" <br/>
you have to select <em style="color:#0066FF">SRI SAI</em> for "Party Name", <em style="color:#0066FF">COBI-0611-0015</em> for "SOBI", <em style="color:#0066FF">15.06.2011</em> for "Date".<br/>
Now enter <em style="color:#0066FF">10</em> for "Returned Quantity"(of item code BROC101) and select <em style="color:#0066FF">Add To Stock</em> for "Goods Status".<br/><br/>
  <li>Once filled click on "SAVE" button to save sales return or "CANCEL" button to exit with out saving the sales return.</li>
</ol>

