<p><b>&nbsp;&nbsp;&nbsp;Add New Receipt</b></p>
<ol><b><p>To Add a New Receipt</p></b></ol>
<ol>
<li>Select the "Date" of Receipt, "Customer" and "Receipt Method".</li>
<li>If the selected value of "Receipt Method" is "Advance", 
<ol>Select the value for "Choice" as either "SOs" or "ON A/C".</ol>
<ol>If the selected value of "Choice" is "SOs" you have select the value for "SO" and enter the value for "Amount Paid".</ol>
OR<br/>
If the selected value of "Receipt Method" is "Receipt",
<ol>Select the value for "Choice" as "COBIs" or "Debit Notes" or "ON A/C".</ol>
<ol>If the selected value of "Choice" is "COBIs" you have select the value for "COBI" and enter the value for "Amount Paid".</ol>
</li> 
<li>Now select "Item Code", "Type" Values and enter the value for "Quantity" for all the expected Outputs using the given Inputs.</li>
<li>Select values for "TDS" and "Reception Mode".</li>
<li>If the selected value for "Reception Mode" is CASH
  <ol>You have to select the value for "Cash Code". </ol>
<ol>As and when the Cash Code value is selected the "Code" and "Description" values will be displayed automatically.</ol>
<ol>Now you have to enter the value of "Amount".</ol>
OR<br/>
If the selected value for "Reception Mode" is CHEQUE
<ol>You have to select the value for "Bank A/C No".</ol>
<ol>As and when the Bank A/C No. value is selected the "Code" and "Description" values will be displayed automatically.</ol>
<ol>Now you have to enter the value of "Amount", "Cheque #" and "Cheque Date".</ol>
OR<br/>
If the selected value for "Reception Mode" is OTHERS
<ol>You have to enter the values for "Code" and "Description".</ol>
<ol>As and when the Bank A/C No. value is selected the "Code" and "Description" values will be displayed automatically.</ol>
</li>
<li>If the selected value of "TDS" is WITH TDS,
<ol>You have to select the value for "Code" and enter the value for "Amount" until the values for all the Payments are filled.</ol></li>
<b>Eg :</b>Suppose the Receipt Date is "06 Jan, 2011", Customer is "MAHESH", Receipt Method is "Receipt", TDS is "Without TDS", Choice is "COBIs", Reception Mode is "Cash", Cash Code is "CIH", Code is "AS111", Description is "1.CASH", Amount is "5000" COBI is "COBI-0111-0020" and Amount Paid is "4000",<br/> 
you have to select <em style="color:#0066FF">06.01.2011</em> for "Date", <em style="color:#0066FF">MAHESH</em> for "Customer", <em style="color:#0066FF">Receipt</em> for "Receipt Method", <em style="color:#0066FF">Without TDS</em> for "TDS", <em style="color:#0066FF">COBIs </em>for "Choice", <em style="color:#0066FF">Cash</em> for "Reception Mode", <em style="color:#0066FF">CIH</em> for "Cash Code". As and when the Cash Code value is selected the value of "Code" will be displayed as <em style="color:#0066FF">AS111</em> and "Description" is displayed as <em style="color:#0066FF">1.CASH</em>.<br/>
Now enter <em style="color:#0066FF">4000</em> for "Amount", <em style="color:#0066FF">COBI-0111-0020</em> for "COBI" and <em style="color:#0066FF">5000</em> for "Amount Paid".</li>
<li>Once filled click "PAY" button to Save the Receipt or click "CANCEL" button to exit without Saving the Receipt.</ol></div><!--end of procuretoppay_Pro_addpayment-->
