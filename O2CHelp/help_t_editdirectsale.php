<p><b>&nbsp;&nbsp;&nbsp;Edit Existing Direct Sale</b></p>
<ol><b>To Edit Existing Direct Sale</b>
<li>You have to select the "Date" of the Sales, "Customer", and enter the value of "Book Invoice"(if not available make it "0"(Zero) ) if you want to change the previous values. </li>
<br/>
<b>Eg:</b> If suppose you want to change the Sales Date is from "1st Jan, 2011" to "5th Jan, 2011" you have to select <em style="color:#0066FF">05.01.2011</em> for Date. 
  <li>You can select "Category" and "Code" if you want to change the previous values.<br/>As and when the "Code" value is selected the "Description" and "Units" of the selected item will be displayed automatically.</li>
<li>Now enter the "Quantity" and "Price/Unit" if you want to change the previous values.<br/>The "Basic Amount" will be calculated based on the "Quantity Received" and "VAT" and displayed in the appropriate box given.</li><br/>
<li>Repeat the steps 2 and 3 until the information of all sale items is filled.</li> 
<li>To enter the Discount in % select "%" Radio button or if you want to enter the Discount Amount select the "Amt" Radio button and enter the appropriate "Discount" value in appropriate input box provided.<br/>
The "Total Price" will be calculated automatically based on the "Discount" and the "Basic Price".</li><br/>
<li>Select value for "Freight" as Included( in the Price/Unit) or Excluded , Account Code and enter "Freight Amount".<br/>
Based on "Total Price" and "Freight Amount" the value of "Grand Total" will be calculated.</li>
<li>You have to select the Payment Mode for Freight from dropdown named "Via" such as CASH, CHEQUE or OTHERS.<br/>
If it is CASH you have to select "Cash Code" and value for "Dated"<br/>
If it is CHEQUE you have to select values for "Bank A/C No", "Dated" and enter value for "Cheque"(Cheque Number).  </li><br/>
<li>Once filled click on 'SAVE' button to save changes to the selected Direct Sale or 'CANCEL' to exit without saving the changes to the selected Direct Sales.</li>
</ol>

