<p><b>&nbsp;&nbsp;&nbsp;Add Salary Parameters</b></p>
<ol><b>To Add Salary Parameters</b>
<li>You have to select "Date", "Sector" and "Employee Name".</li>
<li>Now enter values for "Basic Allowance", "HRA", "Medicial Allowance", "City Compensation Allowance", "Travelling Allowance", "Special Allowance", "Conveyance", "Education Allowance", "Other Allowance", "Provident Fund", "Professional Tax", "Income Tax", "Loan Repayments" and "Other Deductions".</li>
<b>Eg : </b>Suppose if the Date is "07 <sup>th</sup> July, 2011", Sector is "Hatchery", Employee Name is "Rajesh", Basic Allowance is "50", HRA is "15", Medicial Allowance is "18", City Compensation Allowance is "5" and  Travelling Allowance is "12" then <br/>
You have to select <em style="color:#0066FF">07.07.2011</em> for "Date", <em style="color:#0066FF">Hatchery</em> for "Sector", <em style="color:#0066FF">Rajesh</em> for "Employee Name"<br/>
Now enter <em style="color:#0066FF">50</em> for "Basic Allowance", <em style="color:#0066FF">15</em> for "HRA", <em style="color:#0066FF">18</em> for "Medicial Allowance", <em style="color:#0066FF">5</em> for "City Compensation Allowance" and <em style="color:#0066FF">12</em> for "Travelling Allowance". 

 <li>Once filled click on 'SAVE' button to save the Salary Parameters or 'BACK' to exit without saving the Salary Parameters Details.</li>
</ol>
