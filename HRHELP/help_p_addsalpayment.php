<p><b>&nbsp;&nbsp;&nbsp;Add Salary Payment</b></p>
<ol><b>To Add Salary Payment</b>
<li>You have to select "Sector", "Designation" and "Date" .<br/>
As and when the value for "Designation" is selected list of employees will be displayed along with their salary details such as "ID", "Name", "Total Salary", "Deduction", "Actual Salary Paid" and "Advance".</li>
<li>Now select "Payment Mode" and "Code".</li>
<li>If the payment mode is <em style="color:#0066FF">Cheque</em> enter value for "Cheque #".</li>
 <li>Once filled click on 'PAY' button to save the Salary Payment Details or 'CANCEL' to exit without saving the Salary Payment Details.</li>
</ol>
