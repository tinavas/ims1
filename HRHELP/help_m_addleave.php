<p><b>&nbsp;&nbsp;&nbsp;Add New Leave</b></p>
<ol><b>To Add a new Leave to an employee</b>
<li>You have to select either "Single Day" or "Multiple Days".<br/></li>
<br/>
To add <b> Multiple Leaves</b>
<li>Select "Date", enter "Reason", select values for "Approved Date" and "Approved By".</li>
<br/>
To add a<b> Single Leave</b>
<li>Select "FROM DATE", "TO DATE", enter "Reason", select values for "Approved Date" and "Approved By".</li><br/>
<b>Eg :</b>Suppose to add leaves from "20th June, 2011" to "25th June, 2011" approved on date "16th June, 2011" by "Ajay"<br/>
You have to select <em style="color:#0066FF">Multiple Leaves</em>, <em style="color:#0066FF">20.06.2011</em> for "FROM DATE", <em style="color:#0066FF">25.06.2011</em> for "TO DATE", <em style="color:#0066FF">Ajay</em> "Approved By" and <em style="color:#0066FF">16.06.2011</em> for "Approved Date".</li>

 <li>Once filled click on 'SUBMIT' button to save the Leave Details or 'CANCEL' to exit without saving the Leave Details.</li>
</ol>
