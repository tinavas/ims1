<p><b>&nbsp;&nbsp;&nbsp;Add Month attendendance</b></p>
<ol><b>To Add Month attendendance</b>
<br>
<b>Pre-Requirement:</b>You should give no of working days to months(i.e Masters ->Working Days)
<li>
Select "Sector", next select "Designation", then select "Month" and next Select "Year".</li>
<li> Now automatically no of working days will be displayed and details of all employees also be displayed.
</li>
<img src="images/mnth_atten.jpg">
<b>Eg :</b>Suppose sector is "Golden Group", Designation is "Data entry", month is "october" ,year is "2011" then select <em style="color:#0066FF">Golden Group</em> for "sector" , <em style="color:#0066FF">"Data entry</em> for "Designation" , <em style="color:#0066FF">oct</em> for "month" , select <em style="color:#0066FF">2011</em> for "year"<br>
<li>Enter <em style="color:#0066FF">25</em> for "Days Present".</li>
 <li>Once filled click on 'SAVE' button to save the Working Days or 'CANCLE' to exit without saving the Working Days .</li>
</ol>
