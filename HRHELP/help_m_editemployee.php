<p><b>&nbsp;&nbsp;&nbsp;Edit Existing Employee</b></p>
<ol>Click on <b>Employee Details</b>
<li>You have to select the values of "Title", "Sector", "Group", "Salary Type", "designation", "D O J"(Date of Joining), "Rpt. To"(Reporting To), "Salary A/C" and "Loan A/C" if you want any changes from previous values.</li>
<li>Now enter values for "Emp. Name", "Salary", "Advance" and "Savings"  if you want any changes from previous values. </li>
<b>Eg :</b>Suppose if you want to change the employee Designation from "Supervisor", to "Manager" and Salary from "7000" to "10000" then<br/> you have to select <em style="color:#0066FF">Manager</em> for "Designation" and enter <em style="color:#0066FF">10000</em> for "Salary".</li><br/>
&nbsp;<br/>
Click on <b>Personal Details</b>
<li>You have to enter "Father Name".</li>
<li>Now select either "Married" or "Unmarried". If Married enter value for "Spouse"  if you want any changes from previous values.</li>
<li>Now select values for Gender as either "Male" or "Female", "D.O.B", "Blood Group"  if you want any changes from previous values.</li>
<li>Now if employee is having Driving License click on "Drv. Lic." and enter "Drv. Lic. #", "Exp.of Lic" or if employee is not having Driving License  click on "No Drv. Lic."  if you want any changes from previous values.</li>
<li>Now if employee is having Vehicle click on "Vehicle" if employee is having driving License and enter "Vehicle  #", "Veh. Ins. #" or if employee is not having Vehicle click on "No Vehicle "  if you want any changes from previous values.</li>
<li>Now select values for "Qualification", "Bank" and "Country"  if you want any changes from previous values.</li>
<li>Now enter values for "PAN Card/IDEF No.", "(Bank)Branch", "A/C #", "IFSC Code", "Contact #", "Company #" and "Address"  if you want any changes from previous values.</li>
<b>Eg :</b>Suppose if the employees got a driving License with License no "DLFAP10261332011" you have to select <em style="color:#0066FF">Drv. Lic</em> and enter <em style="color:#0066FF">DLFKA74558412011</em> "Drv. Lic. #", <em style="color:#0066FF">06.11.2031</em> for "Exp.of Lic".</li>
<br/>
&nbsp;<br/>
Click on <b>References</b>
<li>You have to enter "Name", "Address", "Contact No 1", "Contact No 1"  and select "Country" for both <b>Reference-1 Details</b> and <b>Reference-2 Details</b>.</li>
<br/>
&nbsp;<br/>
Click on <b>Emergency Contact</b>
<li>You have to enter "Name", "Address", "Contact No 1", "Contact No 1"  and select "Country" for <b>Emergency Contact</b> if you want any changes in previous values.</li>
<br/>
 <li>Once filled click on 'SAVE' button to save the Employee or 'CANCEL' to exit without saving the Employee if you want any changes in previous values.</li>
</ol>
